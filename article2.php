          <p class="article__text"><strong>Почему малыш чихает? </strong> 
Если ребенок каждый день чихает или кашляет, первое подозрение у родителей падает на простуду. Но кроме простуды причиной воспаления слизистой носа и затрудненного дыхания может быть аллергия. Как помочь малышу справиться с недугом и снять симптомы? Поговорим об одном проверенном способе — промывании носика раствором морской соли.</p>
          
			<p class="article__text"><strong>Откуда берется аллергия. </strong>Аллергический ринит — это воспаление слизистой оболочки носа. В носу при дыхании постоянно оседают бактерии. Реснички внутри носа работают так, что каждые 20 минут слизистая оболочка очищается. Организм человека, страдающего аллергией, очень чувствителен к какому-то конкретному веществу — аллергену. Это вещество при попадании на слизистую всасываются моментально, и уже через минуту возникает ответная реакция организма. </p>
			<p class="article__text"><strong>Коварные аллергены. </strong>Аллергический ринит может быть сезонным, например возникать только весной и осенью, или круглогодичным. Сезонную реакцию вызывает пыльца цветущих растений. 
Круглогодичный ринит возникает, когда контакт с аллергеном постоянный — чиханье от домашней или книжной пыли, шерсти животных, закладывание носа после конкретных пищевых продуктов или медикаментов.
Можно заметить, на какой раздражитель реагирует организм, но всё-таки лучше сдать анализы и проверить теорию научным методом.</p>
			<p class="article__text"><strong>Распознаем аллергию. </strong>Если малыш часто чихает, причем по несколько раз подряд, у него заложено и зудит в носу — пора идти к аллергологу-иммунологу и отоларингологу. Когда ребенок маленький, и про зуд и заложенность рассказать еще не может, посмотрите, не отекло ли у него лицо, дышит ли он через нос или через рот, нет ли покраснения глаз и не текут ли слёзки. </p>
			<p class="article__text"><strong>Чистый носик. </strong> Промывание носа — способ профилактики и лечения аллергического ринита. Если в период время цветения растений два раза в день промывать нос, часть раздражителей смоется, и аллергия будет беспокоить меньше или вовсе не проявится. После процедуры лекарства лучше впитываются в сосуды и эффективней действуют.
Промывать носик можно раствором морской соли. Она очищает и увлажняет полость носа и при регулярном использовании вырабатывает иммунитет слизистой к аллергенам. Также морская соль содержит магний, который улучшает состояние при рините, не дает аллергии развиваться.</p>
			<p class="article__text"><strong>Как промыть нос малышу? </strong> Деткам с рождения промывать нос можно только с помощью специальных капель на основе солевого раствора (например, Отривин бэби), а когда ребенок подрастет допустимо применение спрея. Закапываем солевой раствор, а затем аккуратно освобождаем носик малыша от слизи с помощью аспиратора. С 4-х лет можно попробовать провести процедуру с помощью специального чайничка, сначала под руководством  и контролем мамы или папы, а уже потом самостоятельно. Промывание носа каждый вечер — это профилактика аллергического ринита. Отоларингологи рекомендуют вводить этот метод гигиены наравне с чисткой зубов — ведь в носу бактерий скапливается не меньше, чем во рту, поэтому слизистую нужно очищать регулярно.</p>
