          <p class="article__text">Малыши часто дышат ртом: не только когда дыхание затруднено из-за простуды или аллергии, но и когда, например, задумаются. С детства ребенка нужно приучать к носовому дыханию, потому что оно защищает организм от вирусов и простудных заболеваний.</p>
          <p class="article__text"><strong>Что происходит при носовом дыхании. </strong> 
В носу расположено много маленьких волосков — ресничек. Когда мы вдыхаем, воздух проходит через них, а затем попадает в легкие. Пыль, бактерии и вредоносные организмы задерживаются на ресничках и не проникают в организм.
Носовая полость также выделяет слизь, которая убивает микробы. Таким образом половина болезнетворных элементов погибает в носоглотке. При дыхании через рот риск заболеть повышается, потому что там нет такой мощной защиты.
Кроме этого, зимой воздух согревается, когда поступает через нос. Поэтому на улице в мороз врачи рекомендуют не кричать и не давать большую нагрузку на горло — иначе холодный воздух окажется в легких и спровоцирует простуду.</p>
          <p class="article__text"><strong>Почему чихать и кашлять полезно. </strong> 
Оказывается, чихание очищает наш организм от всех бактерий и пылинок, скопившихся в носу на ресничках. 
Мы кашляем, когда в гортани появляются микробы. Они щекочут слизистую оболочку, и нам хочется вытеснить их наружу. Благодаря кашлю мы очищаем гортань, и вредные вещества не доходят до легких. Поэтому лечить нужно причину болезни, например, инфекцию, а не сам кашель.</p>
          <p class="article__text"><strong>Как мы чувствуем запахи. </strong>
В носу есть чувствительная зона, отвечающая за запахи. В воздухе находится множество молекул. Когда мы нюхаем, мы со вдохом и выдохом гоняем частицы вдоль обонятельных рецепторов. Большую роль играют крылья носа, мы приводим их в движение с помощью мышцы.
Замечали, что на море или рядом с озером запахи ощущаются лучше, особенно летом? Это связано с тем, что в теплую погоду летучие вещества ведут себя активней, а влажный воздух помогает нам воспринимать ароматы.</p>
		  <p class="article__text"><strong>Ароматерапия. </strong>
Человек способен воспринимать множество различных запахов: цветочные, фруктовые, миндальные, мускусные, амброзиевые, чесночные, хлорные, серные, запахи тела, зловонные.
Некоторые запахи вызывают положительные эмоции, другие наоборот раздражают. Часто на отношение к запаху влияет то, с чем он ассоциируется. Так, запах гари может кому-то напомнить костер в походе, а кому-то невкусную подгоревшую еду.
Ароматы влияют не только на душевное, но и на физическое самочувствие. От некоторых запахов может закружиться голова, а другие наоборот помогут расслабиться.
С древних времен на Востоке люди использовали в лечебных целях ароматерапию. Благовония обладают полезными свойствами: так, например, масло лаванды успокаивает, лимон повышает работоспособность, а тимьян размягчает кашель.</p>
