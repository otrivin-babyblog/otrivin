          <p class="article__text">В жизни бывает всякое. Бывает и так: родители старались, как могли, поддерживали в детской хороший микроклимат, много гуляли с ребенком, старались не кутать и в меру закаливать, а насморк — вот он, тут как тут, несмотря на все профилактические меры. Что же делать в таком случае?</p>
          
			<p class="article__text"><strong>Для чего?</strong>
Прежде всего, нужно поддерживать оптимальную температуру в комнате (до 22°С) и ни в коем случае не одевать ребенка потеплее. Иначе малыш лишь вспотеет, а значит, организм лишится дополнительной влаги. Если недомогание не сопровождается повышением температуры, прогулки тоже отменять не следует. 

Помимо этого, нужно обратить внимание на то, как именно проявляет себя насморк: есть ли заложенность, слизь жидкая и из носа постоянно льет или густая и скапливается где-то в «глубинах». </p>
			<p class="article__text"><strong>Вариант первый — нос не дышит. </strong>
Это означает, что слизистая сильно отекла и, чтобы восстановить носовое дыхание, необходимо снять отек. Все знают, что самый простой способ сделать это — закапать сосудосуживающие капли. Эффект моментальный и держится какое-то время. Однако применять подобные средства нужно с большой осторожностью: 

1) форма выпуска препарата и концентрация действующего вещества должны соответствовать возрасту, взрослые капли детям не подходят; 

2) продолжительность и частоту применения необходимо согласовывать с врачом, так как сосудосуживающие препараты при неправильном использовании вызывают привыкание и «отучают» слизистую справляться с проблемой самостоятельно, а также нарушают ее питание. 

Тем не менее, бояться капель не надо: есть ситуации, когда без них не обойтись. В первую очередь, это касается лечения отита: снимая отек слизистой носа, мы восстанавливаем нормальную вентиляцию уха и ускоряем лечение воспаления. Кроме того, сосудосуживающие помогают в ситуациях, когда ротовое дыхание, которое, как вы уже знаете, не является нормой, в принципе невозможно или недопустимо: во время кормления, на улице при низкой температуре воздуха, при нарушении сна из-за заложенности носа. </p>
			
			
			
			<p class="article__text"><strong>Вариант второй — из носа льет ручьем</strong>
Как ни странно, это не так уж плохо. Во всяком случае, лучше, чем, когда слизь густеет, засыхает и скапливается в носу. Если выделения из носа обильные и жидкие, нужно проверить, не аллергического ли они происхождения, и при необходимости подключить антигистаминные препараты. 

В остальных случаях достаточно минимизировать раздражение кожи, возникающее при частом высмаркивании: используйте как можно более мягкие носовые платки. </p>
 <img style="float:left; margin-right: 20px; margin-bottom: 20px;" src="<? echo $source; ?>img/3_2.jpg" alt="" class="article__img article__img_left">
 
		<p class="article__text"><strong>Вариант третий — слизь загустела, плохо отходит, мешает дышать, вызывает кашель. </strong>

Самое главное здесь — увлажнение «по всем фронтам»: обильное питье, увлажнители воздуха, солевые растворы в нос с последующим высмаркиванием или применением аспиратора, если ребенок еще не умеет избавляться от выделений в носу самостоятельно. 

Не забывайте, что густая слизь в носоглотке — идеальная среда для размножения бактерий. Поэтому для предотвращения бактериальных осложнений после обычного ОРВИ вы должны сделать все, чтобы она стала более жидкой и легко отделяемой. Это важно еще и потому, что чрезмерные усилия, прилагаемые при высмаркивании густой слизи, увеличивают риск возникновения отита.

Причины насморка, как мы уже выяснили в предыдущих статьях, могут быть разными, а наиболее типичных его проявлений не так уж много. Так что общайтесь с врачом и оказывайте носу первую помощь в соответствии с рекомендациями, не забывая и о простых, повседневных правилах ухода за ребенком. Первое без второго просто-напросто не работает!</p>
 
 
