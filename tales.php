Иван отвечает:<br>
- Это я, твой сын. А на Руси все спокойно. Отец наелся хлеба и лег в могилу. А Иван направился домой, дорогой набрал грибов. Приходит старший брат его спрашивает:<br>
- Видел отца?<br>
- Видел.<br>
