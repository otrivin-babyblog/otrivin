            <h3 class="tales__name">Морозко</h3>
            <img src="<? echo $source; ?>img/sk_morozko.jpg" alt="" class="tales__img">
            <p id="bt2" class="tales__about">
  Живало-бывало, — жил дед да с другой женой. У деда была дочка, и у бабы была дочка.<br>
Все знают, как за мачехой жить: перевернешься — бита и недовернешься — бита. А родная дочь что ни сделает — за все гладят по головке: умница.<br>
Падчерица и скотину поила-кормила, дрова и воду в избу носила, печь топила, избу мела — еще до свету... Ничем старухе не угодишь — все не так, все худо.<br>
Ветер хоть пошумит, да затихнет, а старая баба расходится — не скоро уймется. Вот мачеха и придумала падчерицу со свету сжить.<br>
—&emsp;Вези, вези ее, старик, — говорит мужу, — куда хочешь, чтобы мои глаза ее не видали! Вези ее в лес, на трескучий мороз.<br>
Старик затужил, заплакал, однако делать нечего, бабы не переспоришь. Запряг лошадь:<br>
—&emsp;Садись мила дочь, в сани.<br>
Повез бездомную в лес, свалил в сугроб под большую ель и уехал.<br>
Девушка сидит под елью, дрожит, озноб ее пробирает. Вдруг слышит — невдалеке Морозко по елкам потрескивает, с елки на елку поскакивает, пощелкивает. Очутился на той ели, под которой девица сидит, и сверху ее спрашивает:<br>
—&emsp;Тепло ли тебе, девица?<br>
—&emsp;Тепло, Морозушко, тепло, батюшка.<br>
Морозко стал ниже спускаться, сильнее потрескивает, пощелкивает:<br>
—&emsp;Тепло ли тебе, девица? Тепло ли тебе, красная?<br>
Она чуть дух переводит:<br>
—&emsp;Тепло, Морозушко, тепло, батюшка.<br>
Морозко еще ниже спустился, пуще затрещал, сильнее защелкал:<br>
—&emsp;Тепло ли тебе, девица? Тепло ли тебе, красная? Тепло ли тебе, лапушка?<br>
Девица окостеневать стала, чуть-чуть языком шевелит:<br>
—&emsp;Ой, тепло, голубчик Морозушко!<br>
Тут Морозко сжалился над девицей, окутал ее теплыми шубами, отогрел пуховыми одеялами.<br>
А мачеха по ней уж поминки справляет, печет блины и кричит мужу:<br>
—&emsp;Ступай, старый хрыч, вези свою дочь хоронить!<br>
Поехал старик в лес, доезжает до того места, — под большою елью сидит его дочь, веселая, румяная, в собольей шубе, вся в золоте, в серебре, и около — короб с богатыми подарками.<br>
Старик обрадовался, положил все добро в сани, посадил дочь, повез домой.<br>
А дома старуха печет блины, а собачка под столом:<br>
—&emsp;Тяф, тяф! Старикову дочь в злате, в серебре везут, а старухину замуж не берут.<br>
Старуха бросит ей блин:<br>
—&emsp;Не так тявкаешь! Говори: «Старухину дочь замуж берут, а стариковой дочери косточки везут...»<br>
Собака съест блин и опять:<br>
—&emsp;Тяф, тяф! Старикову дочь в злате, в серебре везут, а старухину замуж не берут.<br>
Старуха блины ей кидала и била ее, собачка — все свое...<br>
Вдруг заскрипели ворота, отворилась дверь, в избу идет падчерица — в злате-серебре, так и сияет. А за ней несут короб высокий, тяжелый. Старуха глянула — и руки врозь...<br>
—&emsp;Запрягай, старый хрыч, другую лошадь! Вези, вези мою дочь в лес да посади на то же место...<br>
Старик посадил старухину дочь в сани, повез ее в лес на то же место, вывалил в сугроб под высокой елью и уехал.<br>
Старухина дочь сидит, зубами стучит.<br>
А Морозко по лесу потрескивает, с елки на елку поскакивает, пощелкивает, на старухину дочь поглядывает:<br>
—&emsp;Тепло ли тебе, девица?<br>
А она ему:<br>
—&emsp;Ой, студено! Не скрипи, не трещи, Морозко...<br>
Морозко стал ниже спускаться, пуще потрескивать, пощелкивать.<br>
—&emsp;Тепло ли тебе, девица? Тепло ли тебе, красная?<br>
—&emsp;Ой, руки, ноги отмерзли! Уйди, Морозко...<br>
Еще ниже спустился Морозко, сильнее приударил, затрещал, защелкал:<br>
—&emsp;Тепло ли тебе, девица? Тепло ли тебе, красная?<br>
—&emsp;Ой, совсем застудил! Сгинь, пропади, проклятый Морозко!<br>
Рассердился Морозко да так хватил, что старухина дочь окостенела.<br>
Чуть свет старуха посылает мужа:<br>
—&emsp;Запрягай скорее, старый хрыч, поезжай за дочерью, привези ее в злате-серебре...<br>
Старик уехал. А собачка под столом:<br>
—&emsp;Тяф, тяф! Старикову дочь женихи возьмут, а старухиной дочери в мешке косточки везут.<br>
Старуха кинула ей пирог:<br>
—&emsp;Не так тявкаешь! Скажи: «Старухину дочь в злате-серебре везут...»<br>
А собачка — все свое:<br>
—&emsp;Тяф, тяф! Старикову дочь женихи возьмут, а старухиной дочери в мешке косточки везут...<br>
Заскрипели ворота, старуха кинулась встречать дочь. Рогожу отвернула, а дочь лежит в санях мертвая.<br>
Заголосила старуха, да поздно.<br>
  Вот и сказке конец, а кто слушал - молодец!
