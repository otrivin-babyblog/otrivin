Баба-яга накормила его, напоила, в бане выпарила; а царевич рассказал ей, что ищет свою жену Василису Премудрую.<br>
«А, знаю! — сказала баба-яга. — Она теперь у Кощея Бессмертного; трудно ее достать, нелегко с Кощеем сладить: смерть его на конце иглы, та игла в яйце, то яйцо в утке, та утка в зайце, тот заяц в сундуке, а сундук стоит на высоком дубу, и то дерево Кощей как свой глаз бережёт».<br>
Указала яга, в каком месте растет этот дуб. Иван-царевич пришел туда и не знает, что ему делать, как сундук достать? Вдруг откуда не взялся — прибежал медведь и выворотил дерево с корнем; сундук упал и разбился вдребезги, выбежал из сундука заяц и во всю прыть наутек пустился: глядь — а за ним уж другой заяц гонится, нагнал, ухватил и в клочки разорвал. Вылетела из зайца утка и поднялась высоко-высоко; летит, а за ней селезень бросился, как ударит её — утка тотчас яйцо выронила, и упало то яйцо в море. Иван-царевич, видя беду неминучую, залился слезами; вдруг подплывает к берегу щука и держит в зубах яйцо; он взял то яйцо, разбил, достал иглу и отломил кончик: сколько ни бился Кощей, сколько ни метался во все стороны, а пришлось ему помереть! Иван-царевич пошел в дом Кощея, взял Василису Премудрую и воротился домой. После того они жили вместе и долго и счастливо.<br>
