            <h3 class="tales__name">Иванушка-дурачок</h3>
            <img src="<? echo $source; ?>img/sk_ivan_durak.jpg" alt="" class="tales__img">
            <p id="bt19" class="tales__about"><span>Был-жил старик со старухою; у них было три сына: двое умные, третий — Иванушко-дурачок. Умные-то овец в поле пасли, а дурак ничего не делал, все на печке сидел да мух ловил.</span><br>
<span>В одно время наварила старуха аржаных клецок и говорит дураку:</span><br>
<span>—&emsp;Ну-ка, снеси эти клецки братьям; пусть поедят.</span><br>
<span>Налила полный горшок и дала ему в руки; побрел он к братьям.</span><br>
<span>День был солнечный; только вышел Иванушко за околицу, увидел свою тень сбоку и думает:</span><br>
<span>«Что это за человек? Со мной рядом идет, ни на шаг не отстает; верно, клецок захотел?»</span><br>
<span>И начал он бросать на свою тень клецки, так все до единой и повыкидал; смотрит, а тень все сбоку идет.</span><br>
<span>—&emsp;Эка ненасытная утроба! — сказал дурачок с сердцем и пустил в нее горшком — разлетелись черепки в разные стороны.</span><br>
<span>Вот приходит с пустыми руками к братьям; те его спрашивают:</span><br>
<span>—&emsp;Ты, дурак, зачем?</span><br>
<span>—&emsp;Вам обед принес.</span><br>
<span>—&emsp;Где же обед? Давай живее.</span><br>
<span>—&emsp;Да вишь, братцы, привязался ко мне дорогою незнамо какой человек, да всё и поел!</span><br>
<span>—&emsp;Какой такой человек?</span><br>
<span>—&emsp;Вот он! И теперь рядом стоит!</span><br>
<span>Братья ну его ругать, бить, колотить; отколотили и заставили овец пасти, а сами ушли на деревню обедать.</span><br>
<span>Принялся дурачок пасти; видит, что овцы разбрелись по полю, давай их ловить да глаза выдирать. Всех переловил, всем глаза выдолбил, собрал стадо в одну кучу и сидит себе, радехонек, словно дело сделал. Братья пообедали, воротились в поле.</span><br>
<span>—&emsp;Что ты, дурак, натворил? Отчего стадо слепое?</span><br>
<span>—&emsp;Да почто им глаза-то? Как ушли вы, братцы, овцы-то врозь рассыпались, я и придумал: стал их ловить, в кучу сбирать, глаза выдирать; во как умаялся!</span><br>
<span>—&emsp;Постой, еще не так умаешься! — говорят братья и давай угощать его кулаками; порядком-таки досталось дураку на орехи!</span><br>
<span>Ни много ни мало прошло времени, послали старики Иванушко-дурачка в город к празднику по хозяйству закупать. Всего закупил Иванушко: и стол купил, и ложек, и чашек, и соли; целый воз навалил всякой всячины. Едет домой, а лошаденка была такая, знать, неудалая, везет — не везет!</span><br>
<span>«А что, — думает себе Иванушко, —  ведь у лошади четыре ноги, и у стола тоже четыре, так стол-от и сам добежит».</span><br>
<span>Взял стол и выставил на дорогу. Едет-едет, близко ли, далеко ли, а вороны так и вьются над ним да все каркают.</span><br>
<span>«Знать, сестрицам поесть-покушать охота, что так раскричались!» — подумал дурачок. Выставил блюда с яствами наземь и начал потчевать:</span><br>
<span>—&emsp;Сестрицы-голубушки! Кушайте на здоровье.</span><br>
<span>А сам все вперед да вперед подвигается.</span><br>
<span>Едет Иванушко перелеском; по дороге все пни обгорелые.</span><br>
<span>«Эх, — думает, — ребята-то без шапок; ведь озябнут, сердечные!»</span><br>
<span>Взял понадевал на них горшки да корчаги. Вот доехал Иванушко до реки, давай лошадь поить, а она не пьет.</span><br>
<span>«Знать, без соли не хочет!» — и ну солить воду. Высыпал полон мешок соли, лошадь все не пьет.</span><br>
<span>—&emsp;Что ж ты не пьешь, волчье мясо? Разве задаром я мешок соли высыпал?</span><br>
<span>Хватил ее поленом, да прямо в голову — и убил наповал. Остался у Иванушка один кошель с ложками, да и тот на себе понес. Идет — ложки назади так и брякают: бряк, бряк, бряк! И он думает, что ложки-то говорят: «Иванушко-дурак!» — бросил их и ну топтать да приговаривать:</span><br>
<span>—&emsp;Вот вам Иванушко-дурак! Вот вам Иванушко-дурак! Еще вздумали дразнить, негодные!</span><br>
<span>Воротился домой и говорит братьям:</span><br>
<span>—&emsp;Все искупил, братики!</span><br>
<span>—&emsp;Спасибо, дурак, да где ж у тебя закупки-то?</span><br>
<span>—&emsp;А стол-от бежит, да, знать, отстал, из блюд сестрицы кушают, горшки да корчаги ребятам в лесу на головы понадевал, солью-то пойло лошади посолил, а ложки дразнятся — так я их на дороге покинул.</span><br>
<span>—&emsp;Ступай, дурак, поскорее! Собери все, что разбросал по дороге.</span><br>
<span>Иванушко пошел в лес, снял с обгорелых пней корчаги, повышибал днища и надел на батог корчаг с дюжину всяких — больших и малых. Несет домой. Отколотили его братья; поехали сами в город за покупками, а дурака оставили домовничать. Слушает дурак, а пиво в кадке так и бродит, так и бродит.</span><br>
<span>—&emsp;Пиво, не броди! Дурака не дразни! — говорит Иванушко.</span><br>
<span>Нет, пиво не слушается; взял да и выпустил все из кадки, сам сел в корыто, по избе разъезжает да песенки распевает.</span><br>
<span>Приехали братья, крепко осерчали, взяли Иванушка, зашили в куль и потащили к реке. Положили куль на берегу, а сами пошли прорубь осматривать.</span><br>
<span>На ту пору ехал какой-то барин мимо на тройке бурых; Иванушко и ну кричать:</span><br>
<span>—&emsp;Садят меня на воеводство судить да рядить, а я ни судить, ни рядить не умею!</span><br>
<span>—&emsp;Постой, дурак, — сказал барин, — я умею и судить и рядить; вылезай из куля!</span><br>
<span>Иванушко вылез из куля, зашил туда барина, а сам сел в его повозку и уехал из виду. Пришли братья, спустили куль под лед и слушают; а в воде так и буркает.</span><br>
<span>—&emsp;Знать, бурка ловит! — проговорили братья и побрели домой.</span><br>
<span>Навстречу им, откуда ни возьмись, едет на тройке Иванушко, едет да прихвастывает:</span><br>
<span>—&emsp;Вот-ста каких поймал я лошадушек! А еще остался там сивко — такой славный!</span><br>
<span>Завидно стало братьям, говорят дураку:</span><br>
<span>—&emsp;Зашивай теперь нас в куль да спускай поскорей в прорубь! Не уйдет от нас сивко...</span><br>
<span>Опустил их Иванушко-дурачок в прорубь и погнал домой пиво допивать да братьев поминать.</span><br>
<span>Был у Иванушка колодец, в колодце рыба елец, а моей сказке конец.</span><br>
 (Из сборника А.Н. Афанасьева «Народные русские сказки».)</p>
