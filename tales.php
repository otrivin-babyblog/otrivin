            <h3 class="tales__name">Зимовье зверей</h3>
            <img src="<? echo $source; ?>img/sk_zimovie.jpg" alt="" class="tales__img">
            <p id="bt15" class="tales__about">
<span>Шел бык лесом, попадается ему навстречу баран.</span><br>

<span>— Куда, баран, идешь? — спросил бык.</span><br>

<span>— От зимы лета ищу, — говорит баран.</span><br>

<span>— Пойдем со мною!</span><br>

<span>Вот пошли вместе, попадается им навстречу свинья.</span><br>

<span>— Куда, свинья, идешь? — спросил бык.</span><br>

<span>— От зимы лета ищу, — отвечает свинья.</span><br>

<span>— Иди с нами.</span><br>

<span>Пошли втроем дальше, навстречу им гусь.</span><br>

<span>— Куда, гусь, идешь? — спрашивает бык.</span><br>

<span>— От зимы лета ищу, — отвечает гусь.</span><br>

<span>— Ну, иди за нами!</span><br>

<span>Вот гусь и пошел за ними. Идут, а навстречу им петух.</span><br>

<span>— Куда, петух, идешь? — спросил бык.</span><br>

<span>— От зимы лета ищу, — отвечает петух.</span><br>

<span>— Иди за нами!</span><br>

<span>Вот они идут путем-дорогою и разговаривают промеж себя:</span><br>

<span>— Как же, братцы-товарищи! Время подходит холодное, где тепла искать?</span><br>

<span>Бык и сказывает:</span><br>

<span>— Ну, давайте избу строить, а то, чего доброго, и впрямь зимою замерзнем.</span><br>

<span>Баран говорит:</span><br>

<span>— У меня шуба тепла — вишь какая шерсть! Я и так перезимую.</span><br>

<span>Свинья говорит:</span><br>

<span>— А по мне хоть какие морозы — я не боюсь: зароюсь в землю и без избы прозимую.</span><br>

<span>Гусь говорит:</span><br>

<span>— А я сяду в середину ели, одно крыло постелю, а другим оденусь, меня никакой холод не возьмет; я и так прозимую.</span><br>

<span>Петух говорит:</span><br>

<span>— А разве у меня нет своих крыльев? И я прозимую!</span><br>

<span>Бык видит — дело плохо, надо одному хлопотать.</span><br>

<span>— Ну, — говорит, — вы как хотите, а я стану избу строить.</span><br>

<span>Выстроил себе избушку и живет в ней. Вот пришла зима холодная, стали пробирать морозы; баран просится у быка:</span><br>

<span>— Пусти, брат, погреться.</span><br>

<span>— Нет, баран, у тебя шуба тепла; ты и так перезимуешь. Не пущу!</span><br>

<span>— А коли не пустишь, то я разбегусь и вышибу из твоей избы бревно; тебе же будет холоднее.</span><br>

<span>Бык думал-думал: «Дай пущу, а то, пожалуй, и меня заморозит», — и пустил барана.</span><br>

<span>Вот свинья прозябла, пришла к быку:</span><br>

<span>— Пусти, брат, погреться.</span><br>

<span>— Нет, не пущу! Ты в землю зароешься и так перезимуешь.</span><br>

<span>А не пустишь, так я рылом все столбы подрою да твою избу сворочу.</span><br>

<span>Делать нечего, надо пустить. Пустил и свинью.</span><br>

<span>Тут пришли к быку гусь и петух:</span><br>

<span>— Пусти, брат, к себе погреться.</span><br>

<span>— Нет, не пущу! У вас по два крыла: одно постелешь, другим оденешься; так и прозимуете!</span><br>

<span>— А не пустишь, — говорит гусь, — так я весь мох из твоих стен повыщиплю, тебе же холоднее будет.</span><br>

<span>— Не пустишь? — говорит петух. — Так я взлечу на чердак и всю землю с потолка сгребу, тебе же холоднее будет.</span><br>

<span>Что делать быку? Пустил жить к себе и гуся и петуха.</span><br>

<span>Вот живут они себе в избушке. Отогрелся в тепле петух и начал песенки распевать.</span><br>

<span>Услыхала лиса, что петух песенки распевает, захотелось ей петушиным мясом полакомиться, да как достать его? Лиса поднялась на хитрости, отправилась к медведю да волку и сказала:</span><br>

<span>— Ну, любезные куманьки! Я нашла для всех поживу: для тебя, медведь, — быка, для тебя, волк, — барана, а для себя — петуха.</span><br>

<span>— Хорошо, кумушка! — говорят медведь и волк. — Мы твоих услуг никогда не забудем. Пойдем же приколем да поедим!</span><br>

<span>Лиса привела их к избушке. Медведь говорит волку:</span><br>

<span>— Иди ты вперед!</span><br>

<span>А волк кричит:</span><br>

<span>— Нет, ты посильнее меня, иди ты вперед!</span><br>

<span>Ладно, пошел медведь; только что в двери — бык наклонил голову и припер его рогами к стенке. А баран разбежался, да как бацнет медведя в бок и сшиб его с ног. А свинья рвет и мечет в клочья. А гусь подлетел — глаза щиплет. А петух сидит на брусу и кричит:</span><br>

<span>— Подайте сюда, подайте сюда!</span><br>

<span>Волк с лисой услыхали крик да бежать!</span><br>

<span>Вот медведь рвался, рвался, насилу вырвался, догнал волка и рассказывает:</span><br>

<span>— Ну, что было мне! Этакого страху отродясь не видывал. Только что вошел я в избу, откуда ни возьмись, баба с ухватом на меня... Так к стене и прижала! Набежало народу пропасть: кто бьет, кто рвет, кто шилом в глаза колет. А еще один на брусу сидел да все кричал: «Подайте сюда, подайте сюда!» Ну, если б подали к нему, кажись бы, и смерть была!</span><br>

