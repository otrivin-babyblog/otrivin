            <p id="bt14" class="tales__about"><span>Жил-был старик. У него было три сына: двое умных, третий — дурачок Емеля.</span><br>

<span>Те братья работают, а Емеля целый день лежит на печке, знать ничего не хочет.</span><br>

<span>Один раз братья уехали на базар, а бабы, невестки, давай посылать его:</span><br>

<span>— Сходи, Емеля, за водой.</span><br>

<span>А он им с печки:</span><br>

<span>— Неохота...</span><br>

<span>— Сходи, Емеля, а то братья с базара воротятся, гостинцев тебе не привезут.</span><br>

<span>— Ну ладно.</span><br>

<span>Слез Емеля с печки, обулся, оделся, взял ведра да топор и пошел на речку.</span><br>

<span>Прорубил лед, зачерпнул ведра и поставил их, а сам глядит в прорубь. И увидел Емеля в проруби щуку. Изловчился и ухватил щуку в руку:</span><br>

<span>— Вот уха будет сладка!</span><br>

<span>Вдруг щука говорит ему человечьим голосом:</span><br>

<span>— Емеля, отпусти меня в воду, я тебе пригожусь.</span><br>

<span>А Емеля смеется:</span><br>

<span>— На что ты мне пригодишься? Нет, понесу тебя домой, велю невесткам уху сварить. Будет уха сладка.</span><br>

<span>Щука взмолилась опять:</span><br>

<span>— Емеля, Емеля, отпусти меня в воду, я тебе сделаю все, что ни пожелаешь.</span><br>

<span>— Ладно, только покажи сначала, что не обманываешь меня, тогда отпущу.</span><br>

<span>Щука его спрашивает:</span><br>

<span>— Емеля, Емеля, скажи — чего ты сейчас хочешь?</span><br>

<span>— Хочу, чтобы ведра сами пошли домой и вода бы не расплескалась...</span><br>

<span>Щука ему говорит:</span><br>

<span>— Запомни мои слова: когда что тебе захочется — скажи только:</span><br>


