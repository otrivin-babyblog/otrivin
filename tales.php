<span>Емеля и говорит:</span><br>


<span>— По щучьему веленью,</span><br>

<span>По моему хотенью —</span><br>

<span>ступайте, ведра, сами домой...</span><br>



<span>Только сказал — ведра сами и пошли в гору. Емеля пустил щуку в прорубь, а сам пошел за ведрами.</span><br>

<span>Идут ведра по деревне, народ дивится, а Емеля идет сзади, посмеивается... Зашли ведра в избу и сами стали на лавку, а Емеля полез на печь.</span><br>

