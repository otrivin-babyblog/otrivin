- Кто из вас, молодцы, с разлету на коне доскочит до оконца да поцелует мою дочь в губы, за того отдам ее замуж и полцарства в придачу.<br>
Тогда добрые молодцы начали скакать. Куда там - высоко, не достать! Попытались Ивановы братья, до середины не доскочили. Дошла очередь до Ивана. Он разогнал Сивку-бурку, гикнул, ахнул, скакнул - двух венцов только не достал. Взвился опять, разлетелся в другой раз - одного венца не достал. Еще завертелся, закружился, разгорячил коня и дал рыскача - как огонь, пролетел мимо окошка, поцеловал царевну Несравненную Красоту в сахарные уста, а царевна ударила его кольцом в лоб, приложила печать. Тут весь народ закричал:<br>
- Держи, держи его!<br>
А его и след простыл. Прискакал Иван в чистое поле, влез Сивке-бурке в левое ухо, а из правого вылез и сделался опять Иваном-дураком. Коня пустил, а сам пошел домой, по дороге набрал грибов. Обвязал лоб тряпицей, залез на печь и полеживает.<br>
