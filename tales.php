- Когда помру, вы три ночи подряд ходите ко мне на могилу, приносите мне хлеба.<br>
Старика этого схоронили. Приходит ночь, надо большему брату идти на могилу, а ему не то лень, не то боится, - он и говорит младшему брату:<br>
- Ваня, замени меня в эту ночь, сходи к отцу на могилу. Я тебе пряник куплю.<br>
