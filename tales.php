и тут же отдал приказ трём своим сыновьям:<br>
«Чтобы жены ваши соткали мне за единую ночь по ковру».<br>
Воротился Иван-царевич невесел, ниже плеч буйну голову повесил.<br>
«Ква-ква, Иван-царевич! Почто так кручинен стал? Аль услышал от отца своего слово жесткое, неприятное?»<br>
— «Как мне не кручиниться? Государь мой батюшка приказал за единую ночь соткать ему шелковый ковер».<br>
