            <h3 class="tales__name">Поди туда-не знаю куда, принеси то-не знаю что</h3>
            <img src="<? echo $source; ?>img/sk_prinesi_to_ne_hz_chto.jpg" alt="" class="tales__img">
            <p id="bt18" class="tales__about"><span>В некотором государстве жил-был царь, холост — не женат. Был у него на службе стрелок по имени Андрей.</span><br>
<span>Пошел раз Андрей-стрелок на охоту. Ходил, ходил целый день по лесу — не посчастливилось, не мог на дичь напасть. Время было к вечеру, идет он обратно — кручинится. Видит — сидит на дереве горлица.</span><br>
<span>«Дай, — думает, — стрельну хоть эту».</span><br>
<span>Стрельнул и ранил ее — свалилась горлица с дерева на сырую землю. Поднял ее Андрей, хотел свернуть ей голову, положить в сумку.</span><br>
<span>А горлица говорит ему человеческим голосом:</span><br>
<span>—&emsp;Не губи меня, Андрей-стрелок, не руби моей головы, возьми меня живую, принеси домой, посади на окошко. Да смотри, как найдет на меня дремота — в ту пору бей меня правой рукой наотмашь: добудешь себе великое счастье.</span><br>
<span>Удивился Андрей-стрелок: что такое? С виду совсем птица, а говорит человеческим голосом. Принес он горлицу домой, посадил на окошечко, а сам стоит дожидается.</span><br>
<span>Прошло немного времени, горлица положила головку под крылышко и задремала. Андрей вспомнил, что она ему наказывала, ударил ее правой рукой наотмашь. Упала горлица наземь и обернулась девицей, Марьей-царевной, да такой прекрасной, что ни вздумать, ни взгадать, только в сказке сказать.</span><br>
<span>Говорит Марья-царевна стрелку:</span><br>
<span>—&emsp;Сумел меня взять, умей и удержать — поспешным пирком да за свадебку. Буду тебе честной да веселой женой.</span><br>
<span>На том они и поладили. Женился Андрей-стрелок на Марье-царевне и живет с молодой женой, потешается. А службы не забывает: каждое утро ни свет ни заря идет в лес, настреляет дичи и несет на царскую кухню.</span><br>
<span>Пожили они так недолго, Марья-царевна говорит:</span><br>
<span>—&emsp;Бедно живешь ты, Андрей!</span><br>
<span>—&emsp;Да, как сама видишь.</span><br>
<span>—&emsp;Добудь-ка рублей сотню, купи на эти деньги разного шелку, я все дело поправлю.</span><br>
<span>Послушался Андрей, пошел к товарищам, у кого рубль, у кого два занял, накупил разного шелку и принес жене. Марья-царевна взяла шелк и говорит:</span><br>
<span>—&emsp;Ложись спать, утро вечера мудренее.</span><br>
<span>Андрей лег спать, а Марья-царевна села ткать. Всю ночь ткала и выткала ковер, какого в целом свете не видывали: на нем все царство расписано, с городами и деревнями, с лесами и нивами, и птицы в небе, и звери на горах, и рыбы в морях; кругом луна и солнце ходят...</span><br>
<span>Наутро Марья-царевна отдает ковер мужу:</span><br>
<span>—&emsp;Понеси на гостиный двор, продай купцам, да смотри — своей цены не запрашивай, а что дадут, то и бери.</span><br>
<span>Андрей взял ковер, повесил на руку и пошел по гостиным рядам.</span><br>
<span>Подбегает к нему один купец:</span><br>
<span>—&emsp;Послушай, почтенный, сколько спрашиваешь?</span><br>
<span>—&emsp;Ты торговый человек, ты и цену давай.</span><br>
<span>Вот купец думал, думал — не может оценить ковра. Подскочил другой, за ним — еще. Собралась купцов толпа великая, смотрят на ковер, дивуются, а оценить не могут.</span><br>
<span>В то время проезжал мимо рядов царский советник, и захотелось ему узнать, про что толкует купечество. Вышел из кареты, насилу пропихался через великую толпу и спрашивает:</span><br>
<span>—&emsp;Здравствуйте, купцы, заморские гости! О чем речь у вас?</span><br>
<span>—&emsp;Так и так, ковра оценить не можем.</span><br>
<span>Царский советник посмотрел на ковер и сам дался диву:</span><br>
<span>—&emsp;Скажи, стрелок, скажи по правде истинной: откуда добыл такой славный ковер?</span><br>
<span>—&emsp;Так и так, моя жена вышила.</span><br>
<span>—&emsp;Сколько же тебе дать за него?</span><br>
<span>—&emsp;А я и сам не знаю. Жена наказала не торговаться: сколько дадут, то и наше.</span><br>
<span>—&emsp;Ну, вот тебе, стрелок, десять тысяч.</span><br>
<span>Андрей взял деньги, отдал ковер и пошел домой. А царский советник поехал к царю и показывает ему ковер.</span><br>
<span>Царь взглянул — на ковре все его царство как на ладони. Он так и ахнул:</span><br>
<span>—&emsp;Ну, что хочешь, а ковра я тебе не отдам!</span><br>
<span>Вынул царь двадцать тысяч рублей и отдает советнику из рук в руки. Советник деньги взял и думает: «Ничего, я себе другой, еще лучше, закажу».</span><br>
<span>Сел опять в карету и поскакал в слободу. Разыскал избушку, где живет Андрей-стрелок, и стучится в дверь. Марья-царевна отворяет ему. Царский советник одну ногу через порог занес, а другую не переносит, замолчал и про свое дело забыл: стоит перед ним такая красавица, век бы глаз от нее не отвел, все бы смотрел да смотрел.</span><br>
<span>Марья-царевна ждала, ждала ответа да повернула царского советника за плечи и дверь закрыла. Насилу он опомнился, нехотя поплелся домой. И с той поры и есть — не наест и пьет — не запьет: все ему представляется стрелкова жена.</span><br>
<span>Заметил это царь и стал выспрашивать, что за кручина у него такая.</span><br>
<span>Советник говорит царю:</span><br>
<span>—&emsp;Ах, видел я у одного стрелка жену, все о ней думаю! И не запить это, не заесть, никаким зельем не заворожить.</span><br>
<span>Пришла царю охота самому посмотреть стрелкову жену. Оделся он в простое платье, поехал в слободу, нашел избенку, где живет Андрей-стрелок, и стучится в дверь. Марья-царевна отворила ему. Царь одну ногу через порог занес, другую и не может, совсем онемел: стоит перед ним несказанная красота.</span><br>
<span>Марья-царевна ждала, ждала ответа, повернула царя за плечи и дверь закрыла.</span><br>
<span>Защемила царя сердечная зазноба. «Чего, — думает, — хожу холост, не женат? Вот бы жениться на этой красавице! Не стрельчихой ей быть, на роду ей написано быть царицей».</span><br>
<span>Воротился царь во дворец и задумал думу нехорошую — отбить жену от живого мужа. Призывает он советника и говорит:</span><br>
<span>—&emsp;Надумай, как извести Андрея-стрелка. Хочу на его жене жениться. Придумаешь — награжу городами и деревнями и золотой казной, не придумаешь — сниму голову с плеч.</span><br>
<span>Закручинился царский советник, пошел и нос повесил. Как извести стрелка, не придумает. Да с горя и завернул в кабак винца испить.</span><br>
<span>Подбегает к нему кабацкая теребень в рваном кафтанишке.</span><br>
<span>—&emsp;О чем, царский советник, пригорюнился, зачем нос повесил?</span><br>
<span>—&emsp;Поди прочь, кабацкая теребень!</span><br>
<span>—&emsp;А ты меня не гони, лучше стаканчик винца поднеси, я тебя на ум наведу.</span><br>
<span>Поднес ему царский советник стаканчик винца и рассказал про свое горе.</span><br>
<span>Кабацкая теребень и говорит ему:</span><br>
<span>—&emsp;Извести Андрея-стрелка дело нехитрое — сам-то он прост, да жена у него больно хитра. Ну, да мы загадаем загадку такую, что ей не справиться. Воротись к царю и скажи: пускай он пошлет Андрея-стрелка на тот свет узнать, как поживает покойный царь-батюшка. Андрей уйдет и назад не вернется.</span><br>
<span>Царский советник поблагодарил кабацкую теребень — и бегом к царю:</span><br>
<span>—&emsp;Так и так, можно стрелка извести.</span><br>
<span>И рассказал, куда нужно его послать и зачем. Царь обрадовался, велел позвать Андрея-стрелка.</span><br>
<span>—&emsp;Ну, Андрей, служил ты мне верой-правдой, сослужи еще службу: сходи на тот свет, узнай, как поживает мой батюшка. Не то мой меч — твоя голова с плеч.</span><br>
<span>Андрей воротился домой, сел на лавку и голову повесил. Марья-царевна его спрашивает:</span><br>
<span>—&emsp;Что невесел? Или невзгода какая?</span><br>
<span>Рассказал ей Андрей, какую царь задал ему службу.</span><br>
<span>Марья-царевна говорит:</span><br>
<span>—&emsp;Есть о чем горевать! Это не служба, а службишка, служба будет впереди. Ложись спать, утро вечера мудренее.</span><br>
<span>Утром рано, только проснулся Андрей, Марья-царевна дает ему мешок сухарей и золотое колечко.</span><br>
<span>—&emsp;Поди к царю и проси себе в товарищи царского советника, а то, скажи, тебе не поверят, что был ты на том свете. А как выйдешь с товарищем в путь-дорогу, брось перед собой колечко, оно тебя доведет.</span><br>
<span>Андрей взял мешок сухарей и колечко, попрощался с женой и пошел к царю просить себе дорожного товарища. Делать нечего, царь согласился, велел советнику идти с Андреем на тот свет.</span><br>
<span>Вот они вдвоем и вышли в путь-дорогу. Андрей бросил колечко — оно катится, Андрей идет за ним полями чистыми, мхами-болотами, реками-озерами, а за Андреем царский советник тащится.</span><br>
<span>Устанут идти, поедят сухарей — и опять в путь.</span><br>
<span>Близко ли, далеко ли, скоро ли, коротко ли, пришли они в густой, дремучий лес, спустились в глубокий овраг, и тут колечко остановилось.</span><br>
<span>Андрей и царский советник сели поесть сухарей. Глядь, мимо их на старом-престаром царе два черта дрова везут — большущий воз — и погоняют царя дубинками, один с правого бока, другой с левого.</span><br>
<span>Андрей говорит:</span><br>
<span>—&emsp;Смотри: никак, это наш покойный царь-батюшка?</span><br>
<span>—&emsp;Твоя правда, это он самый дрова везет.</span><br>
<span>Андрей и закричал чертям:</span><br>
<span>—&emsp;Эй, господа черти! Освободите мне этого покойничка хоть на малое время, мне нужно кой о чем его расспросить.</span><br>
<span>Черти отвечают:</span><br>
<span>—&emsp;Есть нам время дожидаться! Сами, что ли, дрова повезем?</span><br>
<span>—&emsp;А вы возьмите у меня свежего человека на смену.</span><br>
<span>Ну, черти отпрягли старого царя, на его место впрягли в воз царского советника и давай его с обеих сторон погонять дубинками — тот гнется, а везет.</span><br>
<span>Андрей стал спрашивать старого царя про его житье-бытье.</span><br>
<span>—&emsp;Ах, Андрей-стрелок, — отвечает царь, — плохое мое житье на том свете! Поклонись от меня сыну да скажи, что я накрепко заказываю людей не обижать, а то и с ним то же станется.</span><br>
<span>Только успели они поговорить, черти уж назад едут с порожней телегой. Андрей попрощался со старым царем, взял у чертей царского советника, и пошли они в обратный путь.</span><br>
<span>Приходят в свое царство, являются во дворец.</span><br>
<span>Царь увидал стрелка и в сердцах накинулся на него:</span><br>
<span>—&emsp;Как ты смел назад воротиться?</span><br>
<span>Андрей-стрелок отвечает:</span><br>
<span>—&emsp;Так и так, был я на том свете у вашего покойного родителя. Живет он плохо, велел вам кланяться да накрепко наказывал людей не обижать.</span><br>
<span>—&emsp;А чем докажешь, что ходил на тот свет и моего родителя видел?</span><br>
<span>—&emsp;А тем я докажу, что у вашего советника на спине и теперь еще знаки видны, как его черти дубинками погоняли.</span><br>
<span>Тут царь уверился, делать нечего — отпустил Андрея домой. А сам говорит советнику:</span><br>
<span>—&emsp;Вздумай, как извести стрелка, не то мой меч — твоя голова с плеч.</span><br>
<span>Пошел царский советник, еще ниже нос повесил. Заходит в кабак, сел за стол, спросил вина. Подбегает к нему кабацкая теребень:</span><br>
<span>—&emsp;Что, царский советник, пригорюнился? Поднеси-ка мне стаканчик, я тебя на ум наведу.</span><br>
<span>Советник поднес ему стаканчик винца и рассказал про свое горе. Кабацкая теребень ему говорит:</span><br>
<span>—&emsp;Воротись назад и скажи царю, чтобы задал он стрелку вот какую службу — ее не то что выполнить, трудно и выдумать: послал бы его за тридевять земель, в тридесятое царство добыть кота Баюна...</span><br>
<span>Царский советник побежал к царю и рассказал, какую службу задать стрелку, чтобы он назад не вернулся. Царь посылает за Андреем.</span><br>
<span>—&emsp;Ну, Андрей, сослужил ты мне службу, сослужи другую: ступай в тридесятое царство и добудь мне кота Баюна. Не то мой меч — твоя голова с плеч.</span><br>
<span>Пошел Андрей домой, ниже плеч голову повесил и рассказывает жене, какую царь задал ему службу.</span><br>
<span>—&emsp;Есть о чем кручиниться! — Марья-царевна говорит. — Это не служба, а службишка, служба будет впереди. Ложись спать, утро вечера мудренее.</span><br>
<span>Андрей лег спать, а Марья-царевна пошла на кузницу и велела кузнецам сковать три колпака железных, железные клещи и три прута: один железный, другой медный, третий оловянный.</span><br>
<span>Утром рано Марья-царевна разбудила Андрея:</span><br>
<span>—&emsp;Вот тебе три колпака да клещи и три прута, ступай за тридевять земель, в тридесятое царство. Трех верст не дойдешь, станет одолевать тебя сильный сон — кот Баюн на тебя дремоту напустит. Ты не спи, руку за руку закидывай, ногу за ногу волочи, а где и катком катись. А если уснешь, кот Баюн убьет тебя.</span><br>
<span>И тут Марья-царевна научила его, как и что делать, и отпустила в дорогу.</span><br>
<span>Скоро сказка сказывается, не скоро дело делается — пришел Андрей-стрелок в тридесятое царство. За три версты стал его одолевать сон. Надевает Андрей на голову три колпака железных, руку за руку закидывает, ногу за ногу волочит — идет, а где и катком катится.</span><br>
<span>Кое-как выдержал дремоту и очутился у высокого столба.</span><br>
<span>Кот Баюн увидел Андрея, заворчал, зауркал да со столба прыг ему на голову — один колпак разбил и другой разбил, взялся было за третий. Тут Андрей-стрелок ухватил кота клещами, сволок наземь и давай оглаживать прутьями. Наперво сек железным прутом; изломал железный, принялся угощать медным — и этот изломал и принялся бить оловянным.</span><br>
<span>Оловянный прут гнется, не ломится, вокруг хребта обвивается. Андрей бьет, а кот Баюн начал сказки рассказывать: про попов, про дьяков, про поповых дочерей. Андрей его не слушает, знай охаживает прутом.</span><br>
<span>Невмоготу стало коту, видит, что заговорить нельзя, он и взмолился:</span><br>
<span>—&emsp;Покинь меня, добрый человек! Что надо, все тебе сделаю.</span><br>
<span>—&emsp;А пойдешь со мной?</span><br>
<span>—&emsp;Куда хочешь пойду.</span><br>
<span>Андрей пошел в обратный путь и кота за собой повел. Добрался до своего царства, приходит с котом во дворец и говорит царю:</span><br>
<span>—&emsp;Так и так, службу выполнил, добыл вам кота Баюна.</span><br>
<span>Царь удивился и говорит:</span><br>
<span>—&emsp;А ну, кот Баюн, покажи большую страсть.</span><br>
<span>Тут кот свои когти точит, на царя их ладит, хочет у него белую грудь раздирать, из живого сердце вынимать.</span><br>
<span>Царь испугался:</span><br>
<span>—&emsp;Андрей-стрелок, уйми, пожалуйста, кота Баюна!</span><br>
<span>Андрей кота унял и в клетку запер, а сам пошел домой, к Марье-царевне. Живет-поживает, тешится с молодой женой. А царя еще пуще знобит зазноба сердечная. Опять призвал он советника:</span><br>
<span>—&emsp;Что хочешь придумай, изведи Андрея-стрелка, не то мой меч — твоя голова с плеч.</span><br>
<span>Царский советник идет прямо в кабак, нашел там кабацкую теребень в рваном кафтанишке и просит его выручить, на ум навести. Кабацкая теребень стаканчик вина выпил, усы вытер.</span><br>
<span>—&emsp;Ступай, — говорит, — к царю и скажи: пусть пошлет Андрея-стрелка туда — не знаю куда, принести то — не знаю что. Этой задачи Андрей во веки веков не выполнит и назад не вернется.</span><br>
<span>Советник побежал к царю и все ему доложил. Царь посылает за Андреем.</span><br>
<span>—&emsp;Сослужил ты мне две службы, сослужи третью: сходи туда — не знаю куда, принеси то — не знаю что. Сослужишь — награжу по-царски, а не то мой меч — твоя голова с плеч.</span><br>
<span>Пришел Андрей домой, сел на лавку и заплакал. Марья-царевна его спрашивает:</span><br>
<span>—&emsp;Что, милый, невесел? Или еще невзгода какая?</span><br>
<span>—&emsp;Эх, — говорит, — через твою красу все напасти несу! Велел мне царь идти туда — не знаю куда, принести то — не знаю что.</span><br>
<span>—&emsp;Вот это служба так служба! Ну ничего, ложись спать, утро вечера мудренее.</span><br>
<span>Марья-царевна дождалась ночи, развернула волшебную книгу, читала, читала, бросила книгу и за голову схватилась: про цареву загадку в книге ничего не сказано. Марья-царевна вышла на крыльцо, вынула платочек и махнула. Налетели всякие птицы, набежали всякие звери.</span><br>
<span>Марья-царевна их спрашивает:</span><br>
<span>—&emsp;Звери лесные, птицы поднебесные, — вы, звери, всюду рыскаете, вы, птицы, всюду летаете, — не слыхали ль, как дойти туда — не знаю куда, принести то — не знаю что?</span><br>
<span>Звери и птицы ответили:</span><br>
<span>—&emsp;Нет, Марья-царевна, мы про то не слыхивали.</span><br>
<span>Марья-царевна махнула платочком — звери и птицы пропали, как не бывали. Махнула в другой раз — появились перед ней два великана:</span><br>
<span>—&emsp;Что угодно? Что надобно?</span><br>
<span>—&emsp;Слуги мои верные, отнесите меня на середину Океан-моря.</span><br>
<span>Подхватили великаны Марью-царевну, отнесли на Океан-море и стали на середине, на самой пучине, — сами стоят, как столбы, а ее на руках держат. Марья-царевна махнула платочком, и приплыли к ней все гады и рыбы морские.</span><br>
<span>—&emsp;Вы, гады и рыбы морские, вы везде плаваете, на всех островах бываете, не слыхали ль, как дойти туда — не знаю куда, принести то — не знаю что?</span><br>
<span>—&emsp;Нет, Марья-царевна, мы про то не слыхали.</span><br>
<span>Закручинилась Марья-царевна и велела отнести себя домой. Великаны подхватили ее, принесли на Андреев двор, поставили у крыльца.</span><br>
<span>Утром рано Марья-царевна собрала Андрея в дорогу и дала ему клубок ниток и вышитую ширинку.</span><br>
<span>—&emsp;Брось клубок перед собой, — куда он покатится, туда и ты иди. Да смотри, куда бы ни пришел, будешь умываться, чужой ширинкой не утирайся, а утирайся моей.</span><br>
<span>Андрей попрощался с Марьей-царевной, поклонился на четыре стороны и пошел за заставу. Бросил клубок перед собой, клубок покатился — катится да катится, Андрей идет за ним следом.</span><br>
<span>Скоро сказка сказывается, не скоро дело делается. Много царств и земель прошел Андрей. Клубок катится, нитка от него тянется. Стал клубок маленький, с куриную головочку; вот уж до чего стал маленький, не видно и на дороге. Дошел Андрей до леса, видит — стоит избушка на курьих ножках.</span><br>
<span>—&emsp;Избушка, избушка, повернись ко мне передом, к лесу задом!</span><br>
<span>Избушка повернулась, Андрей вошел и видит — на лавке сидит седая старуха, прядет кудель.</span><br>
<span>—&emsp;Фу, фу, русского духу слыхом не слыхано, видом не видано, а нынче русский дух сам пришел! Вот изжарю тебя в печи да съем и на косточках покатаюсь.</span><br>
<span>Андрей отвечает старухе:</span><br>
<span>—&emsp;Что ты, старая баба-яга, станешь есть дорожного человека! Дорожный человек костоват и черен, ты наперед баньку истопи, меня вымой, выпари, тогда и ешь.</span><br>
<span>Баба-яга истопила баньку. Андрей выпарился, вымылся, достал женину ширинку и стал ею утираться.</span><br>
<span>Баба-яга спрашивает:</span><br>
<span>—&emsp;Откуда у тебя ширинка? Ее моя дочь вышивала.</span><br>
<span>—&emsp;Твоя дочь мне жена, мне и ширинку дала.</span><br>
<span>—&emsp;Ах, зять возлюбленный, чем же мне тебя потчевать?</span><br>
<span>Тут баба-яга собрала ужин, наставила всяких кушаньев, вин и медов. Андрей не чванится — сел за стол, давай уплетать. Баба-яга села рядом. Он ест, она выспрашивает: как он на Марье-царевне женился да живут ли они хорошо? Андрей все рассказал: как женился и как царь послал его туда — не знаю куда, добыть то — не знаю что.</span><br>
<span>—&emsp;Вот бы ты помогла мне, бабушка!</span><br>
<span>—&emsp;Ах, зятюшка, ведь про это диво дивное даже я не слыхивала. Знает про это одна старая лягушка, живет она в болоте триста лет... Ну ничего, ложись спать, утро вечера мудренее.</span><br>
<span>Андрей лег спать, а баба-яга взяла два голика, полетела на болото и стала звать:</span><br>
<span>—&emsp;Бабушка, лягушка-скакушка, жива ли?</span><br>
<span>—&emsp;Жива.</span><br>
<span>—&emsp;Выдь ко мне из болота.</span><br>
<span>Старая лягушка вышла из болота, баба-яга ее спрашивает:</span><br>
<span>—&emsp;Знаешь ли, где то — не знаю что?</span><br>
<span>—&emsp;Знаю.</span><br>
<span>—&emsp;Укажи, сделай милость. Зятю моему дана служба: пойти туда — не знаю куда, взять то — не знаю что.</span><br>
<span>Лягушка отвечает:</span><br>
<span>—&emsp;Я б его проводила, да больно стара, мне туда не допрыгать. Донесет твой зять меня в парном молоке до огненной реки, тогда скажу.</span><br>
<span>Баба-яга взяла лягушку-скакушку, полетела домой, надоила молока в горшок, посадила туда лягушку и утром рано разбудила Андрея:</span><br>
<span>—&emsp;Ну, зять дорогой, одевайся, возьми горшок с парным молоком, в молоке —&emsp; лягушка, да садись на моего коня, он тебя довезет до огненной реки. Там коня брось и вынимай из горшка лягушку, она тебе скажет.</span><br>
<span>Андрей оделся, взял горшок, сел на коня бабы-яги. Долго ли, коротко ли, конь домчал его до огненной реки. Через нее ни зверь не перескочит, ни птица не перелетит.</span><br>
<span>Андрей слез с коня, лягушка ему говорит:</span><br>
<span>—&emsp;Вынь меня, добрый молодец, из горшка, надо нам через реку переправиться.</span><br>
<span>Андрей вынул лягушку из горшка и пустил наземь.</span><br>
<span>—&emsp;Ну, добрый молодец, теперь садись мне на спину.</span><br>
<span>—&emsp;Что ты, бабушка, эка маленькая, чай, я тебя задавлю.</span><br>
<span>—&emsp;Не бойся, не задавишь. Садись да держись крепче.</span><br>
<span>Андрей сел на лягушку-скакушку. Начала она дуться. Дулась, дулась — сделалась словно копна сена.</span><br>
<span>—&emsp;Крепко ли держишься?</span><br>
<span>—&emsp;Крепко, бабушка.</span><br>
<span>Опять лягушка дулась, дулась — сделалась еще больше, словно стог сена.</span><br>
<span>—&emsp;Крепко ли держишься?</span><br>
<span>—&emsp;Крепко, бабушка.</span><br>
<span>Опять она дулась, дулась — стала выше темного леса, да как скакнет — и перепрыгнула через огненную реку, перенесла Андрея на тот берег и сделалась опять маленькой.</span><br>
<span>—&emsp;Иди, добрый молодец, по этой тропинке, увидишь терем — не терем, избу — не избу, сарай — не сарай, заходи туда и становись за печью. Там найдешь то — не знаю что.</span><br>
<span>Андрей пошел по тропинке, видит: старая изба — не изба, тыном обнесена, без окон, без крыльца. Он туда вошел и спрятался за печью.</span><br>
<span>Вот немного погодя застучало, загремело по лесу, и входит в избу мужичок с ноготок, борода с локоток, да как крикнет:</span><br>
<span>—&emsp;Эй, сват Наум, есть хочу!</span><br>
<span>Только крикнул, откуда ни возьмись, появляется стол накрытый, на нем бочонок пива да бык печеный, в боку нож точеный. Мужичок с ноготок, борода с локоток, сел возле быка, вынул нож точеный, начал мясо порезывать, в чеснок помакивать, покушивать да похваливать.</span><br>
<span>Обработал быка до последней косточки, выпил целый бочонок пива.</span><br>
<span>—&emsp;Эй, сват Наум, убери объедки!</span><br>
<span>И вдруг стол пропал, как и не бывало, — ни костей, ни бочонка... Андрей дождался, когда уйдет мужичок с ноготок, вышел из-за печки, набрался смелости и позвал:</span><br>
<span>—&emsp;Сват Наум, покорми меня...</span><br>
<span>Только позвал, откуда ни возьмись, появился стол, на нем разные кушанья, закуски и заедки, вина и меды.</span><br>
<span>Андрей сел за стол и говорит:</span><br>
<span>—&emsp;Сват Наум, садись, брат, со мной, станем есть-пить вместе.</span><br>
<span>Отвечает ему невидимый голос:</span><br>
<span>—&emsp;Спасибо тебе, добрый человек! Столько лет я здесь служу, горелой корки не видывал, а ты меня за стол посадил.</span><br>
<span>Смотрит Андрей и удивляется: никого не видно, а кушанья со стола словно кто метелкой сметает, вина и меды сами в рюмку наливаются — рюмка скок, скок да скок.</span><br>
<span>Андрей просит:</span><br>
<span>—&emsp;Сват Наум, покажись мне!</span><br>
<span>—&emsp;Нет, меня никто не может видеть, я то — не знаю что.</span><br>
<span>—&emsp;Сват Наум, хочешь у меня служить?</span><br>
<span>—&emsp;Отчего не хотеть? Ты, я вижу, человек добрый.</span><br>
<span>Вот они поели. Андрей и говорит:</span><br>
<span>—&emsp;Ну, прибирай все да пойдем со мной.</span><br>
<span>Пошел Андрей из избенки, оглянулся:</span><br>
<span>—&emsp;Сват Наум, ты здесь?</span><br>
<span>—&emsp;Здесь. Не бойся, я от тебя не отстану.</span><br>
<span>Дошел Андрей до огненной реки, там его дожидается лягушка:</span><br>
<span>—&emsp;Добрый молодец, нашел то — не знаю что?</span><br>
<span>—&emsp;Нашел, бабушка.</span><br>
<span>—&emsp;Садись на меня.</span><br>
<span>Андрей опять сел на нее, лягушка начала раздуваться, раздулась, скакнула и перенесла его через огненную реку.</span><br>
<span>Тут он лягушку-скакушку поблагодарил и пошел путем-дорогой в свое царство. Идет, идет, обернется:</span><br>
<span>—&emsp;Сват Наум, ты здесь?</span><br>
<span>—&emsp;Здесь. Не бойся, я от тебя не отстану.</span><br>
<span>Шел, шел Андрей, дорога далека — прибились его резвые ноги, опустились его белые руки.</span><br>
<span>—&emsp;Эх, — говорит, — до чего же я уморился!</span><br>
<span>А сват Наум ему:</span><br>
<span>—&emsp;Что же ты мне давно не сказал? Я бы тебя живо на место доставил.</span><br>
<span>Подхватил Андрея буйный вихрь и понес — горы и леса, города и деревни так внизу и мелькают. Летит Андрей над глубоким морем, и стало ему страшно.</span><br>
<span>—&emsp;Сват Наум, передохнуть бы!</span><br>
<span>Сразу ветер ослаб, и Андрей стал спускаться на море. Глядит — где шумели одни синие волны, появился островок, на островке стоит дворец с золотой крышей, кругом сад прекрасный... Сват Наум говорит Андрею:</span><br>
<span>—&emsp;Отдыхай, ешь, пей да на море поглядывай. Будут плыть мимо три купеческих корабля. Ты купцов зазови да угости, употчевай хорошенько — у них есть три диковинки. Ты меня променяй на эти диковинки; не бойся, я к тебе назад вернусь.</span><br>
<span>Долго ли, коротко ли, с западной стороны плывут три корабля. Корабельщики увидали остров, на нем дворец с золотой крышей и кругом сад прекрасный.</span><br>
<span>—&emsp;Что за чудо? — говорят. — Сколько раз мы тут плавали, ничего, кроме синего моря, не видели. Давай пристанем!</span><br>
<span>Три корабля бросили якоря, три купца-корабельщика сели на легкую лодочку, поплыли к острову. А уж Андрей-стрелок их встречает:</span><br>
<span>—&emsp;Пожалуйте, дорогие гости.</span><br>
<span>Купцы-корабельщики идут дивуются: на тереме крыша как жар горит, на деревах птицы поют, по дорожкам чудные звери прыгают.</span><br>
<span>—&emsp;Скажи, добрый человек, кто здесь выстроил это чудо чудное?</span><br>
<span>—&emsp;Мой слуга, сват Наум, в одну ночь построил.</span><br>
<span>Андрей повел гостей в терем:</span><br>
<span>—&emsp;Эй, сват Наум, собери-ка нам попить, поесть!</span><br>
<span>Откуда ни возьмись, явился накрытый стол, на нем — вина и кушанья, чего душа захочет. Купцы-корабельщики только ахают.</span><br>
<span>—&emsp;Давай, — говорят, — добрый человек, меняться: уступи нам своего слугу, свата Наума, возьми у нас за него любую диковинку.</span><br>
<span>—&emsp;Отчего ж не поменяться? А каковы будут ваши диковинки?</span><br>
<span>Один купец вынимает из-за пазухи дубинку. Ей только скажи: «Ну-ка, дубинка, обломай бока этому человеку!» — дубинка сама начнет колотить, какому хочешь силачу обломает бока.</span><br>
<span>Другой купец вынимает из-под полы топор, повернул его обухом кверху — топор сам начал тяпать: тяп да ляп — вышел корабль; тяп да ляп — еще корабль. С парусами, с пушками, с храбрыми моряками. Корабли плывут, пушки палят, храбры моряки приказа спрашивают.</span><br>
<span>Повернул топор обухом вниз — сразу корабли пропали, словно их и не было.</span><br>
<span>Третий купец вынул из кармана дудку, задудел — войско появилось: и конница, и пехота, с ружьями, с пушками. Войска идут, музыка гремит, знамена развеваются, всадники скачут, приказа спрашивают.</span><br>
<span>Купец задудел с другого конца в дудку — нет ничего, все пропало.</span><br>
<span>Андрей-стрелок говорит:</span><br>
<span>—&emsp;Хороши ваши диковинки, да моя стоит дороже. Хотите меняться — отдавайте мне за моего слугу, свата Наума, все три диковинки.</span><br>
<span>—&emsp;Не много ли будет?</span><br>
<span>—&emsp;Как знаете, иначе меняться не стану.</span><br>
<span>Купцы думали, думали: «На что нам дубинка, топор да дудка? Лучше поменяться, со сватом Наумом будем безо всякой заботы день и ночь и сыты и пьяны».</span><br>
<span>Отдали купцы-корабельщики Андрею дубинку, топор и дудку и кричат:</span><br>
<span>—&emsp;Эй, сват Наум, мы тебя берем с собой! Будешь нам служить верой-правдой?</span><br>
<span>Отвечает им невидимый голос:</span><br>
<span>—&emsp;Отчего не служить? Мне все равно, у кого ни жить.</span><br>
<span>Купцы-корабельщики вернулись на свои корабли и давай пировать — пьют, едят, знай покрикивают:</span><br>
<span>—&emsp;Сват Наум, поворачивайся, давай того, давай этого!</span><br>
<span>Перепились все допьяна, где сидели, там и спать повалились.</span><br>
<span>А стрелок сидит один в тереме, пригорюнился.</span><br>
<span>«Эх, — думает, — где-то теперь мой верный слуга, сват Наум?»</span><br>
<span>—&emsp;Я здесь. Чего надобно?</span><br>
<span>Андрей обрадовался:</span><br>
<span>—&emsp;Сват Наум, не пора ли нам на родную сторонушку, к молодой жене? Отнеси меня домой.</span><br>
<span>Опять подхватил Андрея вихрь и понес в его царство, на родную сторону.</span><br>
<span>А купцы проснулись, и захотелось им опохмелиться:</span><br>
<span>—&emsp;Эй, сват Наум, собери-ка нам попить-поесть, живо поворачивайся!</span><br>
<span>Сколько ни звали, ни кричали, все нет толку. Глядят, и острова нет: на месте его шумят одни синие волны.</span><br>
<span>Погоревали купцы-корабельщики: «Эх, надул нас недобрый человек!» — да делать нечего, подняли паруса и поплыли, куда им было надобно.</span><br>
<span>А Андрей-стрелок прилетел на родимую сторону, опустился возле своего домишки, смотрит: вместо домишки обгорелая труба торчит.</span><br>
<span>Повесил он голову ниже плеч и пошел из города на синее море, на пустое место. Сел и сидит. Вдруг, откуда ни возьмись, прилетает сизая горлица, ударилась об землю и оборотилась его молодой женой, Марьей-царевной.</span><br>
<span>Обнялись они, поздоровались, стали друг друга расспрашивать, друг другу рассказывать.</span><br>
<span>Марья-царевна рассказала:</span><br>
<span>—&emsp;С той поры как ты из дому ушел, я сизой горлицей летаю по лесам да по рощам. Царь три раза за мной посылал, да меня не нашли и домишко сожгли.</span><br>
<span>Андрей говорит:</span><br>
<span>—&emsp;Сват Наум, нельзя ли нам на пустом месте у синего моря дворец поставить?</span><br>
<span>—&emsp;Отчего нельзя? Сейчас будет исполнено.</span><br>
<span>Не успели оглянуться — и дворец поспел, да такой славный, лучше царского, кругом зеленый сад, на деревьях птицы поют, по дорожкам чудные звери скачут.</span><br>
<span>Взошли Андрей-стрелок с Марьей-царевной во дворец, сели у окошка и разговаривают, друг на друга любуются. Живут, горя не знают, и день, и другой, и третий.</span><br>
<span>А царь в то время поехал на охоту, на синее море, и видит — на том месте, где ничего не было, стоит дворец.</span><br>
<span>—&emsp;Какой это невежа без спросу вздумал на моей земле строиться?</span><br>
<span>Побежали гонцы, все разведали и докладывают царю, что тот дворец поставлен Андреем-стрелком и живет он в нем с молодой женой, Марьей-царевной.</span><br>
<span>Еще пуще разгневался царь, посылает узнать, ходил ли Андрей туда — не знаю куда, принес ли то — не знаю что.</span><br>
<span>Побежали гонцы, разведали и докладывают:</span><br>
<span>—&emsp;Андрей-стрелок ходил туда — не знаю куда и добыл то — не знаю что.</span><br>
<span>Тут царь и совсем осерчал, приказал собрать войско, идти на взморье, тот дверец разорить дотла, а самого Андрея-стрелка и Марью-царевну предать лютой смерти.</span><br>
<span>Увидал Андрей, что идет на него сильное войско, скорее схватил топор, повернул его обухом кверху. Топор тяп да ляп — стоит на море корабль, опять тяп да ляп — стоит другой корабль. Сто раз тяпнул, сто кораблей поплыло по синему морю.</span><br>
<span>Андрей вынул дудку, задудел — появилось войско: и конница, и пехота, с пушками, со знаменами.</span><br>
<span>Начальники скачут, приказа ждут. Андрей приказал начинать сражение. Музыка заиграла, барабаны ударили, полки двинулись. Пехота ломит царских солдат, конница скачет, в плен забирает. А со ста кораблей пушки так и бьют по столичному городу.</span><br>
<span>Царь видит, войско его бежит, кинулся сам к войску — останавливать. Тут Андрей вынул дубинку:</span><br>
<span>—&emsp;Ну-ка, дубинка, обломай бока этому царю!</span><br>
<span>Дубинка сама пошла колесом, с конца на конец перекидывается по чистому полю; нагнала царя и ударила его в лоб, убила до смерти.</span><br>
<span>Тут и сражению конец пришел. Повалил из города народ и стал просить Андрея-стрелка, чтобы взял он в свои руки все государство.</span><br>
<span>Андрей спорить не стал. Устроил пир на весь мир и вместе с Марьей-царевной правил он этим царством до глубокой старости.</span><br>
 ( в обработке А.Н. Толстого )</p>
