            <h3 class="tales__name">Теремок</h3>
            <img src="img/sk_teremok.jpg" alt="" class="tales__img">
            <p id="bt3" class="tales__about">Ехал мужик с горшками и потерял один горшок. Прилетела муха-горюха и спрашивает:
— Чей домок-теремок? Кто в тереме живет?
Видит — никого нет. Она залетела в горшок и стала там жить-поживать.
Прилетел комар-пискун и спрашивает:
— Чей домок-теремок? Кто в тереме живет?
— Я, муха-горюха. А ты кто?
— Я комар-пискун.
— Ступай ко мне жить.
Вот они стали жить вдвоем.
Прибежала мышка-погрызуха и спрашивает:
— Чей домок-теремок? Кто в тереме живет?
— Я, муха-горюха.
— Я, комар-пискун. А ты кто?
— Я мышка-погрызуха.
— Ступай к нам жить.
Стали жить они втроем.
Прискакала лягушка-квакушка и спрашивает:
— Чей домок-теремок? Кто в тереме живет?
— Я, муха-горюха.
— Я, комар-пискун.
— Я мышка-погрызуха. А ты кто?
— Я, лягушка-квакушка.
— Ступай к нам жить.
Стали они жить вчетвером.
Бежит зайчик и спрашивает:
— Чей домок-теремок? Кто в тереме живет?
— Я, муха-горюха.
— Я, комар-пискун.
— Я мышка-погрызуха.
— Я, лягушка-квакушка. А ты кто?
— Я заюнок-кривоног, по горке скок.
— Ступай к нам жить.
Стали они жить впятером.
Бежала мимо лиса и спрашивает:
— Чей домок-теремок? Кто в тереме живет?
— Я, муха-горюха.
— Я, комар-пискун.
— Я мышка-погрызуха.
— Я, лягушка-квакушка.
— Я, заюнок-кривоног, по горке скок. А ты кто?
— Я лиса — при беседе краса.
— Ступай к нам жить.
Стали они жить вшестером.
Прибежал волк:
— Чей домок-теремок? Кто в тереме живет?
— Я, муха-горюха.
— Я, комар-пискун.
— Я мышка-погрызуха.
— Я, лягушка-квакушка.
— Я, заюнок-кривоног, по горке скок.
— Я, лиса — при беседе краса. А ты кто?
— Я волк-волчище — из-за куста хватыш.
— Ступай к нам жить.
Вот живут они семеро все вместе — и горя мало.
Пришел медведь и стучится:
— Чей домок-теремок? Кто в тереме живет?
— Я, муха-горюха.
— Я, комар-пискун.
— Я мышка-погрызуха.
— Я, лягушка-квакушка.
— Я, заюнок-кривоног, по горке скок.
— Я, лиса — при беседе краса.
— Я, волк-волчище — из-за куста хватыш. А ты кто?
— Я вам всем пригнетыш.
Сел медведь на горшок, горшок раздавил и всех зверей распугал. Вот и сказке Теремок конец, а кто слушал - молодец!</p>
