Уложила его спать, а сама сбросила лягушечью кожу — и обернулась душой-девицей, Василисою Премудрою, вышла на красное крыльцо<br>
и закричала громким голосом:<br>
«Мамки-няньки! Собирайтесь, снаряжайтесь шелковый ковёр ткать — чтоб таков был, на каком я сиживала у родного моего батюшки!»<br>
Как сказано, так и сделано. Наутро проснулся Иван-царевич, у квакуши ковер давно готов — и такой чудный, что ни вздумать, ни взгадать, разве в сказке сказать. Изукрашен ковер златом-серебром, хитрыми узорами. Благодарствовал царь на там ковре Ивану-царевичу и тут же отдал новый приказ, чтобы все три царевича явились к нему на смотр вместе с женами. Опять воротился Иван-царевич невесел, ниже плеч буйну голову повесил. «Кваква, Иван-царевич! Почто кручинишься? Али от отца услыхал слово неприветливое?» — «Как мне не кручиниться? Государь мой батюшка велел, чтобы я с тобой на смотр приходил; как я тебя в люди покажу!» — «Не тужи, царевич! Ступай один к царю в гости, а я вслед за тобой буду, как услышишь стук да гром — скажи: это моя лягушонка в коробчонке едет».<br>
Вот старшие братья явились на смотр с своими женами, разодетыми, разубранными; стоят да с Ивана-царевича смеются:<br>
«Что ж ты, брат, без жены пришёл? Хоть бы в платочке принёс! И где ты этакую красавицу выискал? Чай, все болота исходил?»<br>
Вдруг поднялся великий стук да гром — весь дворец затрясся; гости крепко напугались, повскакивали с мест своих и не знают, что им делать; а Иван-царевич и говорит:<br>
