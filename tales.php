  В некотором царстве, в некотором государстве жил да был царь с царицею, у него было три сына — все молодые, холостые, удальцы такие, что ни в&nbsp; сказке сказать, ни пером написать; младшего звали Иван-царевич.<br>
Говорит им царь таково слово:<br>
«Дети мои милые, возьмите себе по стрелке, натяните тугие луки и пустите в разные стороны; на чей двор стрела упадет, там и сватайтесь».<br>
Пустил стрелу старший брат — упала она на боярский двор, прямо против девичья терема; пустил средний брат — полетела стрела к купцу на двор и остановилась у красного крыльца, а на там крыльце стояла душа-девица, дочь купеческая, пустил младший брат — попала стрела в грязное болото, и подхватила её лягуша-квакуша.<br>
Говорит Иван-царевич:<br>
«Как мне за себя квакушу взять? Квакуша не ровня мне!»<br>
— «Бери! — отвечает ему царь. — Знать, судьба твоя такова».<br>
Вот поженились царевичи: старший на боярышне, средний на купеческой дочери, а Иван-царевич на лягуше-квакуше.<br>
Призывает их царь и приказывает:<br>
«Чтобы жены ваши испекли мне к завтрему по мягкому белому хлебу».<br>
Воротился Иван-царевич в свои палаты невесел, ниже плеч буйну голову повесил.<br>
«Ква-ква, Иван-царевич! Почто так кручинен стал? — спрашивает его лягуша.<br>
— Аль услышал от отца своего слово неприятное?»<br>
