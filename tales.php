Подлетела к царскому крыльцу золочёная коляска, в шесть лошадей запряжена, и вышла оттуда Василиса Премудрая — такая красавица, что ни вздумать, ни взгадать, только в сказке сказать! Взяла Ивана-царевича за руку и повела за столы дубовые, за скатерти бранные. Стали гости есть-пить, веселиться; Василиса Премудрая испила из стакана да последки себе за левый рукав вылила; закусила лебедем да косточки за правый рукав спрятала. Жены старших царевичей увидали ее хитрости, давай и себе то ж делать. После, как пошла Василиса Премудрая танцевать с Иваном-царевичем, махнула левой рукой — сделалось озеро, махнула правой — и поплыли по воде белые лебеди; царь и гости диву дались. А старшие невестки пошли танцевать, махнули левыми руками — гостей забрызгали, махнули правыми — кость царю прямо в глаз попала! Царь рассердился и прогнал их с позором.<br>
Тем временем Иван-царевич улучил минуточку, побежал домой, нашёл лягушечью кожу и спалил ее на большом огне. Приезжает Василиса Премудрая, хватилась — нет лягушечьей кожи, приуныла, запечалилась<br>
и говорит царевичу:<br>
«Ох, Иван-царевич! Что же ты наделал? Если б немножко ты подождал, я бы вечно была твоею; а теперь прощай! Ищи меня за тридевять земель в тридесятом царстве — у Кощея Бессмертного».<br>
Обернулась белой лебедью и улетела в окно. Иван-царевич горько заплакал, помолился богу на все на четыре стороны и пошел куда глаза глядят. Шел он близко ли, далеко ли, долго ли, коротко ли — попадается ему навстречу старый старичок.<br>
«Здравствуй, — говорит, — добрый молодец! Чего ищешь, куда путь держишь?»<br>
Царевич рассказал ему своё несчастье.<br>
«Эх, Иван-царевич! Зачем ты лягушью кожу спалил? Не ты её надел, не тебе и снимать было! Василиса Премудрая хитрей, мудрёней своего отца уродилась; он за то осерчал на неё и велел ей три года квакушею быть. Вот тебе клубок; куда он покатится — ступай за ним смело».<br>
Иван-царевич поблагодарствовал старику и пошёл за клубочком. Идёт чистым полем, попадается ему медведь.<br>
«Дай, — говорит, — убью зверя!»<br>
А медведь провещал ему:<br>
«Не бей меня, Иван-царевич! Когда-нибудь пригожусь тебе».<br>
Идёт он дальше, глядь, а над ним летит селезень; царевич прицелился из ружья, хотел было застрелить птицу, как вдруг провещала она человечьим<br>
голосом:<br>
«Не бей меня, Иван-царевич! Я тебе сама пригожусь».<br>
Он пожалел и пошёл дальше. Бежит косой заяц; царевич опять за ружье, стал целиться, а заяц провещал ему человечьим<br>
голосом:<br>
«Не бей меня, Иван-царевич! Я тебе сам пригожусь».<br>
Иван-царевич пожалел и пошёл дальше — к синему морю, видит — на песке лежит издыхает щука-рыба.<br>
«Ах, Иван-царевич, — провещала щука, — сжалься надо мною, пусти меня в море».<br>
Он бросил ее в море и пошёл берегом. Долго ли, коротко ли — прикатился клубочек к избушке; стоит избушка на куриных лапках, кругом повёртывается.<br>
Говорит Иван-царевич:<br>
«Избушка, избушка! Стань по-старому, как мать поставила, — ко мне передом, а к морю задом».<br>
Избушка повернулась к морю задом, к нему передом. Царевич взошёл в неё и видит: на печи, на девятом кирпичи, лежит баба-яга костяная нога, нос в потолок врос, сама зубы точит.<br>
