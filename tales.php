            <h3 class="tales__name">Волшебная дудочка</h3>
            <img src="<? echo $source; ?>img/sk_magik_dudochka.jpg" alt="" class="tales__img">
            <p id="bt20" class="tales__about"><td class="text" id="PAR">
<span>Как слышал сказку, так и рассказываю.</span><br>
<span>В стародавние годы жили были муж с женой. И росла у них дочка пригожая. Всем девица взяла: и ростом, и дородством, и угожеством.</span><br>
<span>Глядя на нее, люди радовались: со всеми девушка приветливая, ласковая, обходительная. Всем торопилась помочь чем могла.</span><br>
<span>Но вот пристигло несчастье, пришла беда. Умерла у девушки мать.</span><br>
<span>Много ли, мало времени прошло — женился отец на вдовице. А вдовица свою дочь в дом привела. И стало в семье четверо.</span><br>
<span>Сиротой жить нерадостно, а при мачехе стало и того хуже.</span><br>
<span>Родную дочь она нежила, тешила, а падчерицу невзлюбила с первого дня.</span><br>
<span>С петухами сирота вставала, слезами умывалась, до полуночи по хозяйству управлялась. И пряла, и ткала, и по воду ходила, и дрова носила, и коров доила.</span><br>
<span>А злая баба только покрикивала:</span><br>
<span>—&emsp;Неумелица ты, негодница! Хлебоежа на мою голову досталась!</span><br>
<span>Вот открыл как-то раз отец сундук, что от первой жены остался. А в сундуке и душегрея, мехом отороченная, и кокошник, жемчугами унизанный, и полсапожки сафьяновые, и перстенек золотой с камушком дорогим, и одежа разная.</span><br>
<span>—&emsp;Поделим поровну, и будет у наших дочерей приданое, — сказал отец.</span><br>
<span>А завистливые мачеха со своей дочерью затаили черную думу.</span><br>
<span>—&emsp;Экое богатство делить на две доли, — мачеха шептала дочери. — Да с таким-то приданым мы и купеческого сына найдем. Не за мужика выйдешь, за лапотника. Только не оплошай!</span><br>
<span>Прошло сколько-то времени после того разговора, собрались девушки по ягоды идти. А отец шутейно им и говорит:</span><br>
<span>—&emsp;Ну вот, кто из вас больше ягод принесет, той при дележе приданного чуть побольше достанется.</span><br>
<span>Ходят девушки по лесу, аукаются, берут ягоды. А как завечерело, сошлись они на полянке. Глянула мачехина дочь — батюшки светы, у стариковой дочери корзинка полным-полна, а у нее всего ничего, лишь на донышке! Тут и припомнились материны речи: не делить приданого на две доли...</span><br>
<span>И как проходили через болото, выхватила мачехина дочь у сводной сестры корзину с ягодами и столкнула ее с перекладин-жердочек в бездонную топь.</span><br>
<span>—&emsp;Тону я, погибаю, сестрица милая, — взмолилась девица, — помоги мне!</span><br>
<span>—&emsp;Стану я тебе помогать! Тони, из этой топи не выкарабкаешься. А все приданое мне одной достанется! — крикнула мачехина дочь.</span><br>
<span>Перебралась через болото и бегом побежала домой. Дорогой пересыпала в свой кузов ягоды — чистые, крупные, одна к одной, а корзинку сводной сестры закопала в мох.</span><br>
<span>—&emsp;Умница, моя разумница! — встретила ее мать. — Посмотри, старик, сколько ягод моя дочка набрала!</span><br>
<span>—&emsp;А чего не вместе пришли? — спросил отец.</span><br>
<span>—&emsp;Разошлись мы с ней, — ответила мачехина дочь, — аукалась я, аукалась, да никто мне не откликнулся; думаю, раньше меня набрала корзинку и ушла домой.</span><br>
<span>—&emsp;Ну где ей, доченька, раньше тебя управиться. Уснула где-нибудь, вот и не услышала тебя! — засмеялась баба.</span><br>
<span>Вечер прошел и ночь прошла. Поутру старик рано встал.</span><br>
<span>—&emsp;Надо идти искать, — говорит, — видно, беда стряслась.</span><br>
<span>Собрал соседей. Пошли они в лес. И бабина дочь с ними.</span><br>
<span>—&emsp;Вот здесь, рассказывает, — мы разошлись и больше не виделись.</span><br>
<span>Ходили-ходили день с утра до вечера, да так ни с чем и воротились.</span><br>
<span>Лето уже на исходе. Идет-бредет по тем тропам старичок странник. Ступил на жердочки-перекладины, а на топлом месте растет травяная дудка. Срезал ту дудку старик, приложил к губам и только подул в нее, как слышит: заиграла, запела дудка, жалобно запричитала:</span><br>
<br><span>—&emsp;Поиграй, поиграй, дедушка,</span><br>
<span>Поиграй, поиграй, родимый.</span><br>
<span>Нас было две сводные сестрицы,</span><br>
<span>И вот меня загубили,</span><br>
<span>За красные ягодки</span><br>
<span>Да за матушкино приданое</span><br>
<span>В гнилом болоте утопили!</span><br>
<br><span>И вот пришел старик странник поздно вечером в ту деревню, попросился в крайнюю избу ночевать, как раз в тот дом, где сирота-девица потерялась.</span><br>
<span>После ужина заговорил старик странник:</span><br>
<span>—&emsp;Неподалеку от вашей деревни срезал я дудочку. Такая забавная: сама поет-выговаривает. Возьми-ка, хозяин, подуй в эту дудочку!</span><br>
<span>Чуть только подул хозяин в дудочку, как заговорила, запела она:</span><br>
<br><span>—&emsp;Поиграй, поиграй, мой батюшка,</span><br>
<span>Поиграй, поиграй, родимый.</span><br>
<span>Нас было две сводные сестрицы,</span><br>
<span>И вот меня загубили,</span><br>
<span>За красные ягодки</span><br>
<span>Да за матушкино приданое</span><br>
<span>В гнилом болоте утопили!</span><br>
<br><span>С лица старик сменился. Протянул дудочку падчерице:</span><br>
<span>—&emsp;Ну-ка, ты поиграй!</span><br>
<span>Только поднесла она дудочку к губам, как заиграла, запела дудочка:</span><br>
<br><span>—&emsp;Поиграй, поиграй, сестрица сводная,</span><br>
<span>Поиграй, поиграй, лиходейка,</span><br>
<span>Поиграй, поиграй, душегубка!</span><br>
<span>Ты меня убила,</span><br>
<span>В гнилом болоте утопила,</span><br>
<span>За красные ягодки</span><br>
<span>Да за матушкино приданое</span><br>
<span>Жизни лишила!</span><br>
<br><span>Кинулся отец за понятыми. Девку-лиходейку, а заодно и мать, злую бабу, связали, приставили караул.</span><br>
<span>А отец с понятыми да со стариком странником на болото побежали. Поискали, поискали и в скором времени вытащили девушку. Обмыли ее, обрядили. Тут она открыла глаза, промолвила:</span><br>
<span>—&emsp;Ой, как долго мне спалось да много во сне виделось! Не держи, родимый батюшка, ни бабы-лиходейки, ни дочери-злодейки. Не будет от них житья ни тебе, ни мне.</span><br>
<span>Простил отец на радости злую бабу и падчерицу-злодейку, прогнал их со двора:</span><br>
<span>—&emsp;Ступайте, откуда пришли!</span><br>
</td> ( В пересказе А.Н. Нечаева. )</p>
