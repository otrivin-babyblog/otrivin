            <h3 class="tales__name">Финист — ясный сокол</h3>
            <img src="<? echo $source; ?>img/sk_finist_sokol.jpg" alt="" class="tales__img">
            <p id="bt16" class="tales__about"><span>Жил да был крестьянин. Умерла у него жена, осталось три дочки. Хотел старик нанять работницу — в хозяйстве помогать. Но меньшая дочь, Марьюшка, сказала:</span><br>
<span>—&emsp;Не надо, батюшка, нанимать работницу, сама я буду хозяйство вести.</span><br>
<span>Ладно. Стала дочка Марьюшка хозяйство вести. Все-то она умеет, все-то у нее ладится. Любил отец Марьюшку: рад был, что такая умная да работящая дочка растет. Из себя-то Марьюшка красавица писаная. А сестры ее завидущие да жаднющие; из себя-то они некрасивые, а модницы-перемодницы — весь день сидят да белятся, да румянятся, да в обновки наряжаются, платье им — не платье, сапожки — не сапожки, платок — не платок.</span><br>
<span>Поехал отец на базар и спрашивает дочек:</span><br>
<span>—&emsp;Что вам, дочки купить, чем порадовать?</span><br>
<span>И говорят старшая и средняя дочки:</span><br>
<span>—&emsp;Купи по полушалку, да такому, чтоб цветы покрупнее, золотом расписанные.</span><br>
<span>А Марьюшка стоит да молчит. Спрашивает ее отец:</span><br>
<span>—&emsp;А что тебе, доченька, купить?</span><br>
<span>—&emsp;Купи мне, батюшка, перышко Финиста — ясна сокола.</span><br>
<span>Приезжает отец, привозит дочкам полушалки, а перышка не нашел.</span><br>
<span>Поехал отец в другой раз на базар.</span><br>
<span>—&emsp;Ну, — говорит, — дочки, заказывайте подарки.</span><br>
<span>Обрадовались старшая и средняя дочки:</span><br>
<span>—&emsp;Купи нам по сапожкам с серебряными подковками.</span><br>
<span>А Марьюшка опять заказывает:</span><br>
<span>—&emsp;Купи мне, батюшка, перышко Финиста - ясна сокола.</span><br>
<span>Ходил отец весь день, сапожки купил, а перышка не нашел. Приехал без перышка.</span><br>
<span>Ладно. Поехал старик в третий раз на базар, а старшая и средняя дочки говорят:</span><br>
<span>—&emsp;Купи нам по платью.</span><br>
<span>А Марьюшка опять просит:</span><br>
<span>—&emsp;Батюшка, купи перышко Финиста — ясна сокола.</span><br>
<span>Ходил отец весь день, а перышка не нашел. Выехал из города, а навстречу старенький старичок.</span><br>
<span>—&emsp;Здорово, дедушка!</span><br>
<span>—&emsp;Здравствуй, милый! Куда путь-дорогу держишь?</span><br>
<span>—&emsp;К себе, дедушка, в деревню. Да вот горе у меня: меньшая дочка наказывала купить перышко Финиста — ясна сокола, а я не нашел.</span><br>
<span>—&emsp;Есть у меня такое перышко, да оно заветное; но для доброго человека, куда ни шло, отдам.</span><br>
<span>Вынул дедушка пёрышко и подает, а оно самое обыкновенное. Едет крестьянин и думает: «Что в нем Марьюшка нашла хорошего!»</span><br>
<span>Привез старик подарки дочкам; старшая и средняя наряжаются да над Марьюшкой смеются:</span><br>
<span>—&emsp;Как была ты дурочка, так и есть. Нацепи свое перышко в волоса да красуйся!</span><br>
<span>Промолчала Марьюшка, отошла в сторону; а когда все спать полегли, бросила Марьюшка перышко на пол и проговорила:</span><br>
<span>—&emsp;Любезный Финист — ясный сокол, явись ко мне, жданный мой жених!</span><br>
<span>И явился ей молодец красоты неописанной. К утру молодец ударился об пол и сделался соколом. Отворила ему Марьюшка окно, и улетел сокол к синему небу.</span><br>
<span>Три дня Марьюшка привечала к себе молодца; днем он летает соколом по синему поднебесью, а к ночи прилетает к Марьюшке и делается добрым молодцем.</span><br>
<span>На четвертый день сестры злые заметили — наговорили отцу на сестру.</span><br>
<span>—&emsp;Милые дочки, — говорит отец, — смотрите лучше за собой.</span><br>
<span>«Ладно, — думают сестры, — посмотрим, как будет дальше».</span><br>
<span>Натыкали они в раму острых ножей, а сами притаились, смотрят.</span><br>
<span>Вот летит ясный сокол. Долетел до окна и не может попасть в комнату Марьюшки. Бился-бился, всю грудь изрезал, а Марьюшка спит и не слышит. И сказал тогда сокол:</span><br>
<span>—&emsp;Кому я нужен, тот меня найдет. Но это будет нелегко. Тогда меня найдешь, когда трое башмаков железных износишь, трое посохов железных изломаешь, трое колпаков железных порвешь.</span><br>
<span>Услышала это Марьюшка, вскочила с кровати, посмотрела в окно, а сокола нет, и только кровавый след на окне остался. Заплакала Марьюшка горькими слезами — смыла слезками кровавый след и стала еще краше.</span><br>
<span>Пошла она к отцу и проговорила:</span><br>
<span>—&emsp;Не брани меня, батюшка, отпусти в путь-дорогу дальнюю. Жива буду — свидимся, умру — так, знать, на роду написано.</span><br>
<span>Жалко было отцу отпускать любимую дочку, но отпустил.</span><br>
<span>Заказала Марьюшка трое башмаков железных, трое посохов железных, трое колпаков железных и отправилась в путь-дорогу дальнюю, искать желанного Финиста — ясна сокола. Шла она чистым полем, шла темным лесом, высокими горами. Птички веселыми песнями ей сердце радовали, ручейки лицо белое умывали, леса темные привечали. И никто не мог Марьюшку тронуть: волки серые, медведи, лисицы — все звери к ней сбегались. Износила она башмаки железные, посох железный изломала и колпак железный порвала.</span><br>
<span>И вот выходит Марьюшка на поляну и видит: стоит избушка на курьих ножках — вертится. Говорит Марьюшка:</span><br>
<span>—&emsp;Избушка, избушка, встань к лесу задом, ко мне передом! Мне в тебя лезть, хлеба есть.</span><br>
<span>Повернулась избушка к лесу задом, к Марьюшке передом. Зашла Марьюшка в избушку и видит: сидит там баба-яга — костяная нога, ноги из угла в угол, губы на грядке, а нос к потолку прирос.</span><br>
<span>Увидела баба-яга Марьюшку, зашумела:</span><br>
<span>—&emsp;Тьфу, тьфу, русским духом пахнет! Красная девушка, дело пытаешь аль от дела лытаешь?</span><br>
<span>—&emsp;Ищу, бабушка, Финиста — ясна сокола.</span><br>
<span>—&emsp;О красавица, долго тебе искать! Твой ясный сокол за тридевять земель, в тридевятом государстве. Опоила его зельем царица-волшебница и женила на себе. Но я тебе помогу. Вот тебе серебряное блюдечко и золотое яичко. Когда придешь в тридевятое царство, наймись работницей к царице. Покончишь работу — бери блюдечко, клади золотое яичко, само будет кататься. Станут покупать — не продавай. Просись Финиста — ясна сокола повидать.</span><br>
<span>Поблагодарила Марьюшка бабу-ягу и пошла. Потемнел лес, страшно стало Марьюшке, боится и шагнуть, а навстречу кот. Прыгнул к Марьюшке и замурлыкал:</span><br>
<span>—&emsp;Не бойся, Марьюшка, иди вперед. Будет еще страшнее, а ты иди и иди, не оглядывайся.</span><br>
<span>Потерся кот спинкой и был таков, а Марьюшка пошла дальше. А лес стал еще темней. Шла, шла Марьюшка, сапоги железные износила, посох поломала, колпак порвала и пришла к избушке на курьих ножках. Вокруг тын, на кольях черепа, и каждый череп огнем горит.</span><br>
<span>Говорит Марьюшка:</span><br>
<span>—&emsp;Избушка, избушка, встань к лесу задом, ко мне передом! Мне в тебя лезть, хлеба есть.</span><br>
<span>Повернулась избушка к лесу задом, к Марьюшке передом. Зашла Марьюшка в избушку и видит: сидит там баба-яга — костяная нога, ноги из угла в угол, губы на грядке, а нос к потолку прирос.</span><br>
<span>Увидела баба-яга Марьюшку, зашумела:</span><br>
<span>—&emsp;Тьфу, тьфу, русским духом пахнет! Красная девушка, дело пытаешь аль от дела лытаешь?</span><br>
<span>—&emsp;Ищу, бабушка, Финиста — ясна сокола.</span><br>
<span>—&emsp;А у моей сестры была?</span><br>
<span>—&emsp;Была, бабушка.</span><br>
<span>—&emsp;Ладно, красавица, помогу тебе. Бери серебряные пяльцы, золотую иголочку. Иголочка сама будет вышивать серебром и золотом по малиновому бархату. Будут покупать — не продавай. Просись Финиста — ясна сокола повидать.</span><br>
<span>Поблагодарила Марьюшка бабу-ягу и пошла. А в лесу стук, гром, свист, черепа лес освещают. Страшно стало Марьюшке. Глядь, собака бежит:</span><br>
<span>—&emsp;Ав, ав, Марьюшка, не бойся, родная иди! Будет еще страшнее, не оглядывайся.</span><br>
<span>Сказала и была такова. Пошла Марьюшка, а лес стал еще темнее. За ноги ее цепляет, за рукава хватает... Идет Марьюшка, идет и назад не оглянется.</span><br>
<span>Долго ли, коротко ли шла — башмаки железные износила, посох железный поломала, колпак железный порвала. Вышла на полянку, а на полянке избушка на курьих ножках, вокруг тын, а на кольях лошадиные черепа; каждый череп огнем горит.</span><br>
<span>Говорит Марьюшка:</span><br>
<span>—&emsp;Избушка, избушка, встань к лесу задом, ко мне передом!</span><br>
<span>Повернулась избушка к лесу задом, а к Марьюшке передом. Зашла Марьюшка в избушку и видит: сидит баба-яга — костяная нога, ноги из угла в угол, губы на грядке, а нос к потолку прирос. Сама черная, а во рту один клык торчит.</span><br>
<span>Увидела баба-яга Марьюшку, зашумела:</span><br>
<span>—&emsp;Тьфу, тьфу, русским духом пахнет! Красная девушка, дело пытаешь аль от дела лытаешь?</span><br>
<span>—&emsp;Ищу, бабушка, Финиста — ясна сокола.</span><br>
<span>—&emsp;Трудно, красавица, тебе будет его отыскать, да я помогу. Вот тебе серебряное донце, золотое веретенце. Бери в руки, само прясть будет, потянется нитка не простая, а золотая.</span><br>
<span>—&emsp;Спасибо тебе, бабушка.</span><br>
<span>—&emsp;Ладно, спасибо после скажешь, а теперь слушай, что тебе накажу: будут золотое веретенце покупать — не продавай, а просись Финиста — ясна сокола повидать.</span><br>
<span>Поблагодарила Марьюшка бабу-ягу и пошла, а лес зашумел, загудел; поднялся свист, совы закружились, мыши из нор повылезли — да все на Марьюшку. И видит Марьюшка — бежит навстречу серый волк.</span><br>
<span>—&emsp;Не горюй, — говорит он, — а садись на меня и не оглядывайся.</span><br>
<span>Села Марьюшка на серого волка, и только ее и видели. Впереди степи широкие, луга бархатные, реки медовые, берега кисельные, горы в облака упираются. А Марьюшка скачет и скачет. И вот перед Марьюшкой хрустальный терем. Крыльцо резное, оконца узорчатые, а в оконце царица глядит.</span><br>
<span>—&emsp;Ну, — говорит волк, — слезай, Марьюшка, иди и нанимайся в прислуги.</span><br>
<span>Слезла Марьюшка, узелок взяла, поблагодарила волка и пошла к хрустальному дворцу. Поклонилась Марьюшка царице и говорит:</span><br>
<span>—&emsp;Не знаю, как вас звать, как величать, а не нужна ли вам будет работница?</span><br>
<span>Отвечает царица:</span><br>
<span>—&emsp;Давно я ищу работницу, но такую, которая могла бы прясть, ткать, вышивать.</span><br>
<span>—&emsp;Все это я могу делать.</span><br>
<span>—&emsp;Тогда проходи и садись за работу.</span><br>
<span>И стала Марьюшка работницей. День работает, а наступит ночь — возьмет Марьюшка серебряное блюдечко и золотое яичко и скажет:</span><br>
<span>—&emsp;Катись, катись, золотое яичко, по серебряному блюдечку, покажи мне моего милого.</span><br>
<span>Покатится яичко по серебряному блюдечку, и предстанет Финист — ясный сокол. Смотрит на него Марьюшка и слезами заливается:</span><br>
<span>—&emsp;Финист мой, Финист — ясный сокол, зачем ты меня оставил одну, горькую, о тебе плакать!</span><br>
<span>Подслушала царица ее слова и говорит:</span><br>
<span>—&emsp;Продай ты мне, Марьюшка, серебряное блюдечко и золотое яичко.</span><br>
<span>—&emsp;Нет, — говорит Марьюшка, — они непродажные. Могу я тебе их отдать, если позволишь на Финиста — ясна сокола поглядеть.</span><br>
<span>Подумала царица, подумала.</span><br>
<span>—&emsp;Ладно, — говорит, — так и быть. Ночью, как он уснет, я тебе его покажу.</span><br>
<span>Наступила ночь, и идет Марьюшка в спальню к Финисту — ясну соколу. Видит она — спит ее сердечный друг сном непробудным. Смотрит Марьюшка — не насмотрится, целует в уста сахарные, прижимает к груди белой, — спит, не пробудится сердечный друг.</span><br>
<span>Наступило утро, а Марьюшка не добудилась милого...</span><br>
<span>Целый день работала Марьюшка, а вечером взяла серебряные пяльцы да золотую иголочку. Сидит вышивает, сама приговаривает:</span><br>
<span>—&emsp;Вышивайся, вышивайся, узор, для Финиста — ясна сокола. Было бы чем ему по утрам вытираться.</span><br>
<span>Подслушала царица и говорит:</span><br>
<span>—&emsp;Продай, Марьюшка, серебряные пяльцы, золотую иголочку.</span><br>
<span>—&emsp;Я не продам, — говорит Марьюшка, — а так отдам, разреши только с Финистом — ясным соколом свидеться.</span><br>
<span>Подумала та, подумала.</span><br>
<span>—&emsp;Ладно, — говорит, — так и быть, приходи ночью.</span><br>
<span>Наступает ночь. Входит Марьюшка в спаленку к Финисту — ясну соколу, а тот спит сном непробудным.</span><br>
<span>—&emsp;Финист ты мой, ясный сокол, встань, пробудись!</span><br>
<span>Спит Финист — ясный сокол крепким сном. Будила его Марьюшка — не добудилась.</span><br>
<span>Наступает день.</span><br>
<span>Сидит Марьюшка за работой, берет в руки серебряное донце, золотое веретенце. А царица увидала:</span><br>
<span>—&emsp;Продай да продай!</span><br>
<span>—&emsp;Продать не продам, а могу и так отдать, если позволишь с Финистом — ясным соколом хоть часок побыть.</span><br>
<span>—&emsp;Ладно, — говорит та.</span><br>
<span>А сама думает: «Все равно не разбудит».</span><br>
<span>Настала ночь. Входит Марьюшка в спальню к Финисту — ясну соколу, а тот спит сном непробудным.</span><br>
<span>—&emsp;Финист ты мой, ясный сокол, встань, пробудись!</span><br>
<span>Спит Финист, не просыпается.</span><br>
<span>Будила, будила — никак не может добудиться, а рассвет близко.</span><br>
<span>Заплакала Марьюшка:</span><br>
<span>—&emsp;Любезный ты мой Финист — ясный сокол, встань, пробудись, на Марьюшку свою погляди, к сердцу своему ее прижми!</span><br>
<span>Упала Марьюшкина слеза на голое плечо Финиста — ясна сокола и обожгла. Очнулся Финист — ясный сокол, осмотрелся и видит Марьюшку. Обнял ее, поцеловал:</span><br>
<span>—&emsp;Неужели это ты, Марьюшка! Трое башмаков железных износила, трое посохов железных изломала, трое колпаков железных поистрепала и меня нашла? Поедем же теперь на родину.</span><br>
<span>Стали они домой собираться, а царица увидела и приказала в трубы трубить, об измене своего мужа оповестить.</span><br>
<span>Собрались князья да купцы, стали совет держать, как Финиста — ясна сокола наказать.</span><br>
<span>Тогда Финист — ясный сокол говорит:</span><br>
<span>—&emsp;Которая, по-вашему, настоящая жена: та ли, что крепко любит, или та, что продает да обманывает?</span><br>
<span>Согласились все, что жена Финиста — ясна сокола — Марьюшка.</span><br>
<span>И стали они жить-поживать да добра наживать. Поехали в свое государство, пир собрали, в трубы затрубили, в пушки запалили, и был пир такой, что и теперь помнят.</span><br>

